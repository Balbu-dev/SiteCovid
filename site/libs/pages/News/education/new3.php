                <div class="bg-secondary text-white pt-2 pb-2">
                    <p class="font-weight-bold text-center align-middle display-4">Professora do DF contrai Covid-19 e pais questionam transparência de escola<p>
                </div>
                <div class="container">
                    <div class="container pt-3 pb-3">
                        <div class="container mb-3">
                            <img src="../../../images/news/WhatsApp-Image-2020-08-06-at-07.jpg" class="rounded mx-auto d-block img-fluid" alt="Saúde Logo" id="logo">
                        </div>
                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Pais de alunos de uma escola privada do Distrito Federal denunciam falta de transparência por parte da instituição em relação a um caso da Covid-19 dentro da unidade. Na segunda-feira (5/10), os responsáveis receberam um comunicado do Colégio Arvense sobre o afastamento de uma professora que estava “de atestado médico”, mas não foram informados que a docente suspeitava ter contraído o novo coronavírus. Apenas nesta sexta (9/10), após a profissional testar positivo, a escola enviou um novo aviso, confirmando a infecção da professora.</p>
                                <p>Após receberem o informativo, nesta manhã, pais procuraram a unidade de ensino para entender o motivo pelo qual a escola não revelou o contexto do afastamento da professora, uma vez que a instituição sabia que ela desconfiava ter contraído a doença e que faria teste.</p>
                                <p>No plano de retomada de atividades da própria escola de educação infantil estão estabelecidas medidas emergenciais em caso de contágio por coronavírus. Entre elas, o afastamento e a orientação quanto à busca de serviço de saúde para investigação diagnóstica tanto para pessoas com sintomas como para aquelas que “convivem com as que apresentem sintomas de síndrome gripal”.</p>
                                <p>Ao Metrópoles, a mãe de uma aluna do 2º ano do colégio disse que considerava permitir que a filha voltasse à rotina presencial em breve, mas que, agora, se sente insegura. “Segundo o plano de retomada, havendo casos sintomáticos, eles deveriam avisar aos que convivem [com quem tem sintomas], então, entendo que os alunos deveriam ter sido avisados e não foram”, disse a mulher, que pediu para não ser identificada.</p>
                                <p>“Entendemos que não foi honesto por parte da escola não informar a situação desde o começo. Não foram transparentes. A minha filha ainda estava tendo aulas em casa, e estávamos prestes a tomar uma decisão sobre o retorno, mas, dentro dessa situação, não sinto segurança em [deixá-la] retornar”, assinalou.</p>
                                <p>Segundo o presidente da Associação de Pais e Alunos das Instituições de Ensino do Distrito Federal (Aspa-DF) Alexandre Veloso, os responsáveis já temiam a volta às aulas após a Justiça desobrigar a testagem em massa dos profissionais. Conforme a decisão judicial, só serão testados os trabalhadores com suspeita ou que tiveram contato com pacientes da doença.</p>
                                <p>No caso do Colégio Arvense, Veloso avalia que a instituição deveria ter sido mais clara com os pais. “Em tempos de Covid-19, a gente tem que visar o coletivo e informar esses contextos de afastamento caso envolva suspeita. A escola já tinha que ter sinalizado: ‘Por motivos de precaução, afastamos a professora, vamos acompanhar para ver o resultado do exame e comunicamos à comunidade’…”, pontuou.</p>
                                <p>“Vamos comunicar o Ministério Público do Trabalho quando tomarmos conhecimento formal desse caso para que realizem o aprimoramento desse protocolo. Esse caso prático mostra a necessidade de reforçar junto às escolas a necessidade dessa boa comunicação com os pais”, completou ele.</p>
                                <h2>O que diz a escola</h2>
                                <p>Para a diretora Márcia Nogueira, não houve falta de transparência da escola. “Acho que é questão de interpretação dos pais”, avaliou.</p>
                                <p>“No sábado (3/10), a professora me ligou dizendo que estava com dor de cabeça e mal-estar, mas o médico teria dito que estava muito precoce. No dia 5, eu mandei um comunicado para os pais, dizendo que ela estava de atestado, que ficaria afastada. Ontem, às 20h, saiu o resultado do exame. Então, enviei outro comunicado, dizendo que ela testou positivo”, detalhou.</p>
                                <p>Questionada por que razões não informou aos pais que a docente estava com sintomas e faria o teste de Covid-19, ela argumentou que “não posso antecipar que ela está com suspeita disso, suspeita daquilo”.</p>
                                <p>“Não vou ficar alarmando ninguém, porque, todo dia, todo mundo tem alguma coisa, então, eu agi corretamente. Não falei o que era, porque não sabia”, justificou-se.</p>
                                <p>Segundo Márcia, a professora fez o teste pelo plano de saúde da escola. Perguntada se o Arvense faria, agora, testes em professores que tiveram contato com a docente contaminada e se orientaria os pais para que as crianças também fossem testadas, ela explicou que, “todo mundo que tiver que ser testado, será”.</p>
                                <h2>Sindicato dos professores apura</h2>
                                <p>De acordo com o diretor jurídico do Sindicato dos Professores em Estabelecimentos Particulares de Ensino do Distrito Federal (Sinproep-DF), Rodrigo de Paula, a entidade ainda não recebeu denúncias formais sobre o caso do Colégio Arvense, porém, o sindicato vai apurar a situação.</p>
                                <p>“Vamos investigar e acionar o Ministério Público do Trabalho para que seja feita uma inspeção. Caso confirmemos [irregularidades], vamos pedir testagem de todos os professores que tiveram contato”, assinalou.</p>
                                <p>Segundo Rodrigo, desde o retorno às aulas presenciais no DF, o Sinproep tomou conhecimento de mais de 10 professores contaminados pelo novo coronavírus. “Mas, nesses casos, a escola afastou, imediatamente, e comunicou tudo aos pais”, comentou.</p>
                                <h2>Cuidados necessários</h2>
                                <p>O cronograma de retorno foi aprovado em 24 de agosto. Por meio de audiência de conciliação virtual, entidades representativas das escolas e dos docentes da rede pública definiram a volta às aulas a partir de 21 de setembro, para a educação infantil e o ensino fundamental 1, enquanto o ensino fundamental 2 retorna em 19 de outubro. Já o ensino médio e os cursos profissionalizantes retomarão as classes presenciais em 26 de outubro.</p>
                                <p>As aulas presenciais só podem ocorrer se as escolas fornecerem luvas e gorros descartáveis, protetores faciais (face shields), jalecos, aventais e outros aparatos necessários para professores, instrutores e demais profissionais que trabalhem diretamente com alunos da educação infantil.</p>
                                <p>Devem ainda ser exigidos equipamentos de proteção individual (EPIs) necessários aos trabalhadores (empregados diretos ou terceirizados), obrigatórios para cada tipo de atividade, principalmente para limpeza, retirada e troca do lixo, manuseio e manipulação de alimentos ou livros e aferição de temperatura.</p>
