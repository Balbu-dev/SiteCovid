                            <div class="row h-75 text-justify">
                                <p>A pequena Maria Beatriz Niederauer, 5 anos, teve que se adaptar a uma nova rotina, com aulas por vídeo, desde o início da pandemia de covid-19, em março deste ano. As idas à escola, com o encontro animado com os coleguinhas pela manhã, deram lugar às videoaulas e a novos hábitos para evitar a contaminação pelo novo coronavírus.</p>
                                <p>Ela aprendeu os cuidados que deve tomar, como usar máscara ao sair de casa, passar álcool em gel e lavar as mãos com frequência. Maria Beatriz conta que gosta das aulas remotas, mas sente falta do contato mais próximo com a professora e os colegas. “Estou gostando das aulas pelo computador. [Mas] eu prefiro a aula na escola porque dá pra abraçar a tia e encontrar os coleguinhas. Gosto das tarefinhas que eu acho muito fáceis e também da hora do parquinho”. E se alguém perguntar porque ela ainda não voltou para as aulas presenciais, a responda está na ponta da língua: “Eu não voltei a ter aula na escola porque eu estou de quarentena do coronavírus”, diz.</p>
                                <p>Olívia Verdelio Lemos, 7 anos, também entende porque passou meses longe da escola, estudando em casa. Ela retornou às aulas presenciais em setembro. “Já voltei para as aulas presenciais. Eu não gosto das aulas online porque fico longe da professora. Não dá pra abraçar”, conta. Ela explica quais cuidados têm que tomar na escola: “Tem que passar álcool em gel e usar máscara. Depois do lanche tem que colocar outra máscara”.</p>
                                <p>Lucas Gabriel dos Reis Cerqueira, 11 anos, diz que as aulas remotas “não são tão ruins”. “Mas não são as melhores do mundo porque não têm contato com as pessoas para conversar cara a cara”, afirma. Ele diz que o coronavírus “pode ser perigoso” e também sabe os cuidados que deve tomar: “passar álcool, manter distância” entre as pessoas.</p>
                                <p>João Matos, 5 anos, retomou às aulas presenciais na última quarta-feira (7), depois seis meses longe da escola. O retorno às aulas presenciais passou por um período de adaptação com a participação dos pais A primeira visita à escola durou 15 minutos para ver as adaptações feitas pela instituição. Depois, João ficou por apenas uma hora na escola. Até que chegou o dia de voltar a frequentar a escola no tempo normal. Neste vídeo, no caminho de volta para casa, a mãe de João, Christiana Matos, 40 anos, artista plástica, pergunta a ele como foi o dia na escola.</p>
                                <h2>No exterior</h2>
                                <p>Fora do Brasil, a rotina das crianças também precisou de adaptações. Lara Freitas, 7 anos, é brasileira e mora com os pais, também brasileiros, nos Estados Unidos há quatro anos. Ela ficou cinco meses em casa, por conta da pandemia. Quando voltou às aulas, no início de setembro, a escola tinha adaptado as salas, com separação entre as mesas. A sala de Lara tem apenas cinco crianças e as aulas presenciais ocorrem em semanas alternadas. Lara conta que sentiu "um pouquinho de medo" ao retornar à escola. "Mas têm poucas crianças", completa. Ela diz que gosta das aulas online porque pode ficar com os pais, mas também gosta de ir à escola para ver os amigos. "Os amigos estão fazendo tudo para não pegar a doença", diz Lara, que sabe que precisa lavar as mãos por 20 segundos e é preciso manter distanciamento dos colegas.</p>
                                <h2>Voltar ou não às aulas presenciais?</h2>
                                <p>A psicóloga clínica e escolar Carla Ciollete diz que muitas crianças se adaptaram bem à rotina de aulas em casa. “É muito mais tranquilo, não enfrenta trânsito, [não precisa] colocar uniforme, os horários são mais flexíveis e o olhar do professor é diferenciado”. Mas ela diz que essa adaptação foi mais fácil para crianças acima de 10 anos, que já estão acostumados a ter contato com redes sociais e jogos.</p>
                                <p>Carla Ciollete afirma ainda que há um lado positivo nesse processo de adaptação que é a maior aproximação dos pais com as crianças, valorizando o “estar juntos”.</p>
                                <p>A psicóloga orienta que a decisão dos pais de retorno às aulas presenciais ou de manutenção dos filhos em casa com aulas remotas deve ser comunicada aos filhos, com explicação dos motivos. Ela destaca que qualquer que seja a decisão, os pais devem passar confiança aos filhos. “Quanto aos pais que não estão se sentido seguros, é direito deles ficar em casa. Aqueles que preferem que os filhos voltem às aulas presenciais devem explicar ao filho o motivo e transmitir segurança de que vai dar certo”, disse.</p>
