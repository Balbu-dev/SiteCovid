                <div class="bg-secondary text-white pt-2 pb-2">
                    <p class="font-weight-bold text-center align-middle display-4">Escolas Estaduais Poderão Retomar As Aulas Presenciais No Dia 19 Em Nilópolis<p>
                </div>
                <div class="container">
                    <div class="container pt-3 pb-3">
                        <div class="container mb-3">
                            <img src="../../../images/news/760204092019-Escola-Estadual-Chico-Anysio-8969-XL-1024x580.jpg" class="rounded mx-auto d-block img-fluid w-50" alt="Saúde Logo" id="logo">
                        </div>
                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Nesta sexta-feira (09), o governador em exercício, Cláudio Castro, e o secretário de Estado de Educação, Comte Bittencourt, anunciaram que o retorno das aulas presenciais na rede pública estadual está previsto para o dia 19 deste mês, somente para as turmas da 3ª série do Ensino Médio, nas modalidades regular e Educação de Jovens e Adultos (EJA – Fase IV). Os colégios localizados em municípios que não autorizaram a flexibilização das regras do isolamento social ou estiverem na classificação laranja de contágio deverão continuar com atividades exclusivamente remotas.</p>
                                <p>Para preparar a retomada das aulas, o Governo do Estado estabeleceu protocolos, publicados em Diário Oficial, garantindo as regras de segurança para alunos, professores e funcionários.</p>
                                <p>“A Educação irá voltar. E a questão principal é como retornar. Tenho dito que esse retorno precisa ser responsável. São quase 42 mil alunos da rede que vão fazer o Enem. Além disso, quero ressaltar que a Secretaria de Educação trabalha a todo vapor para apresentar um plano de retorno para o ano que vem, com 100% dos alunos ou perto disso. Não dá para brincar com a área de Educação. O ensino e o conhecimento são prioridades do Governo do Estado”, afirmou o governador em exercício, Claudio Castro.</p>
                                <p>Na coletiva, Castro anunciou que irá liberar 5 mil GLPs (Gratificação de Lotação Prioritária) aos professores que, em razão do retorno às atividades presenciais, ampliarem suas jornadas de trabalho além das atividades remotas. O recebimento de GLP será feito conforme regulamentação específica a ser expedida pela Seeduc.</p>
                                <p>O secretário Comte Bittencourt explicou que os novos materiais, produzidos em parceria com a Secretaria de Estado de Ciência, Tecnologia e Inovação, por meio do Cecierj/Consórcio Cederj, serão enviados para as escolas estaduais na próxima semana.</p>
                                <p>“Nosso time está se desdobrando para que encontremos soluções para cada região do estado e esse material chegue a todos os alunos, do 6º ano do Fundamental à 3ª série do Ensino Médio. Esse calendário vai ser comunicado na próxima semana. Confio nos educadores do Estado do Rio e conheço as carências”, declarou Comte.</p>
                                <p>De acordo com ele, em 2021, será feito um diagnóstico individual com cada aluno, para que seja possível estabelecer um itinerário pedagógico e corrigir o déficit nas disciplinas principais.</p>
                                <p>“Faremos uma busca ativa pelos alunos que não tiveram acesso aos conteúdos físicos e virtuais para garantir o vínculo deles com a escola. Vale lembrar que o ciclo escolar básico não se limita ao ano de escolarização. O aluno do 6º ano do Fundamental continua mais sete anos na rede. Neste momento, queremos dar o mínimo de condições para os que estão inscritos no Enem. Para aqueles que não se inscreveram, estamos dando a opção de retornarem ano que vem”, explicou.</p>
                                <p>Por meio de parceria entre as secretarias de Educação e de Saúde, serão disponibilizados mais de 30 mil testes rápidos (Igm/IgG) para Covid-19. A testagem dos servidores deverá ocorrer nas unidades municipais de Saúde mais próximas das escolas ou das residências dos profissionais. Uma nova resolução com os locais para os testes e demais orientações sairá na próxima semana.</p>
                                <p>De acordo com o Secretário de Educação, Comte Bittencourt, as unidades de ensino terão uma sala de emergência para os casos de jovens ou professores que apresentarem sintomas.</p>
                                <p>“Temos 710 mil alunos, 126 mil no último ano do Ensino Médio, ou seja, estamos falando de menos de 20% dos alunos. Estamos absolutamente preparados para receber este grupo pequeno. Todas as escolas terão sala de emergência, caso algum caso seja identificado. A unidade de saúde daquela escola estará referenciada para que seja acionada imediatamente”, destacou o secretário.</p>
                                <h2>Comorbidades</h2>
                                <p>Não precisarão retornar às aulas presenciais os profissionais da Educação enquadrados nos grupos vulneráveis da Covid-19, como imunodeprimidos, indivíduos com 60 anos ou mais que possuem doenças crônicas, gestantes e puérperas. Também foi recomendado que quem tiver contato ou more com alguém que tenha testado positivo para o novo coronavírus, se ausente das aulas presenciais. Já o funcionário que não estiver em grupos vulneráveis e tenha resultado negativo do exame da Covid, deverá retornar às aulas presenciais.</p>
                                <h2>Adequação das escolas</h2>
                                <p>O Governo do Estado também destinou o reforço de R$ 9 milhões para cerca de 1,2 mil escolas estaduais para a compra dos materiais.  Já a partir desta semana, as escolas estão adequando seus espaços para garantir o distanciamento social e adquirindo Equipamentos de Proteção Individual (EPIs), como luvas e face shield para os profissionais, além de máscaras para servidores e alunos. As escolas também contarão com dispensers para álcool em gel 70% e tapetes sanitizantes.</p>
