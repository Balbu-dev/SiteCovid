                    <p class="font-weight-bold text-center align-middle display-4">Contágio de Covid-19 em escolas do Rio acende alerta sobre protocolo da rede pública<p>
                </div>
                <div class="container">
                    <div class="container pt-3 pb-3">
                        <div class="container mb-3">
                            <img src="../../../images/news/composition-creativity-desk-education-207756.jpg" class="rounded mx-auto d-block img-fluid" alt="Saúde Logo" id="logo">
                        </div>
                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Na primeira semana em que escolas privadas retomaram aulas na capital fluminense, ao menos três colégios suspenderam turmas por causa de contágio de Covid-19 em alunos ou funcionários. Isso fez crescer o alerta sobre a rede público, principalmente a estadual, que anunciou, nesta sexta, o retorno das aulas para os jovens do 3º ano do Ensino Médio a partir do dia 19. Segundo especialistas, o surgimento de casos será natural, e o importante é não relaxar nas medidas de proteção. O governo estadual não terá um protocolo específico para o que fazer após um aluno ser contagiado, mas a orientação é de que a turma em questão tenha suas aulas suspensas.</p>
                                <p>— Em tese, caso o aluno tenha comprovada a contaminação, evidentemente que os protocolos serão de afastar aquela turma —afirmou o secretário estadual de Educação Comte Bittencourt, que defende a autonomia das direções escolares na decisão das medidas. — Suspensão da turma ou de todas as aulas é uma questão que depende muito do acompanhamento do dia a dia. Vamos entrar no novo normal.</p>
                                <p>As resoluções que a Secretaria estadual de Educação (SES) publicou até aqui tiveram mais foco nas medidas preventivas, ou seja, no afastamento de 1,5 metro por cadeira, no uso de máscaras e oferta de álcool gel. No caso de um aluno ou funcionário apresentar sintomas dentro da escola, haverá uma "sala de emergência", separada para que a pessoa fique em isolamento até a unidade de saúde mais próxima seja contatada.</p>
                                <p>Outra orientação será a definição de horários alternados de intervalos para as turmas, principalmente para as escolas com mais alunos. Assim, se uma turma tiver o recreio em determinado horário, a outra só o terá num momento posterior, e assim por diante. O objetivo é reduzir ao máximo o contato entre os jovens, e isolar os grupos.</p>
                                <p>Para a infectologista Tania Vergara, presidente da Sociedade Brasileira de Infectologia (SBI), o importante é seguir à risca todas as medidas preventivas de distanciamento e higiene, pois isso tornará o risco de infecção menor. Mas, se houver registro de Covid-19 dentro da escola, ela acredita que o mais sensato é se debater caso a caso.</p>
                                <p>— Acho que não precisaria suspender a escola toda, pode funcionar isolando aquela turma específica. Mas isso varia de escola para escola, não dá para der uma regra engessada, é preciso ver qual a condição de cada local. As instalações são diferentes, é preciso pensar primeiro em afastar o infectado — explica.</p>
                                <p>O virologista da UFRJ, Amilcar Tanuri, concorda. Ele destaca que, na Europa, o protocolo vem sendo de afastar o aluno infectado, e testar os outros colegas de classe antes de se tomar uma decisão. O que não se pode perder de vista, porém, é a necessidade de fechamento no caso de confirmação de surtos, explica.</p>
                                <p>— Mas por enquanto a incidência nas escolas está pequena. O certo é isolar só o contaminado inicialmente, e depois testar todos com quem ele teve contato. É preciso também focar nos funcionários e fazer com que as turmas não se misturem muito — explicou o especialista, que citou desenvolvimento de novos tipos de testes, o que pode ajudar no processo. — Agora temos o teste de antígeno, em que faz teste rápido na amostra swab para se identificar o vírus, e não o anticorpo. São novas ferramentas que ajudam. Outra medida certa seria fazer teste de sorologia em uma amostragem de alunos da escola, e depois repetir três meses depois, para analisar a incidência.</p>
                                <h2>Ainda há municípios que não definiram volta</h2>
                                <p>Mesmo com a decisão da retomada da rede estadual, o secretário Comte Bittencourt afirmou que será respeitada a decisão de cada prefeitura do estado, se a autoridade de saúde local entender que não há, ainda, condições de reabertura de suas escolas estaduais. Ele, que admite que não voltarão às aulas todos os 126 mil alunos de 3º ano da rede, diz que, na Região Serrana, algumas prefeituras haviam demonstrado reticência, mas que há expectativa de que o protocolo do governo seja convincente para o retorno seguro.</p>
                                <p>— Ainda farei contato com prefeitos que estão com dúvidas, para explicarmos todo nosso protocolo. Isso certamente vai tranquilizá-los. Na Região Serrana, alguns haviam falado que não voltariam, mas com o entendimento que é só as turmas do 3º ano, ou seja, uma quantidade de alunos muito menor do que se fosse a volta total, acho que teremos menos problemas — explicou o secretário.</p>
                                <h2>Capital ainda sem data</h2>
                                <p>Já na rede municipal do Rio, ainda não há definição sobre a volta das aulas. Procurada, a prefeitura afirmou que as decisões ainda dependem da avaliação do Comitê Científico e da Secretaria municipal de Saúde. Quando houver o retorno, as escolas terão os equipamentos de proteção necessários, as salas passarão por redimensionamento e os protocolos da Vigilância Sanitária serão cumpridos.</p>
                                <p>Enquanto isso, a prefeitura quer ter o controle das informações do que está sendo feito na rede privada. Na sexta, foi publicado em edição extra do Diário Oficial, um formulário de "autodeclaração de protocolo sanitário" (Faps), para que as escolas preencham, obrigatoriamente, as medidas que estão sendo tomadas de prevenção contra o Coronavírus. Com as informações em mãos, a Vigilância Sanitária poderá tomar ações de orientação e fiscalização. As instituições terão sete dias para o preenchimento, sob pena de multa que varia de R$593 a R$2.698. A partir de novembro, o preenchimento será mensal, até o quinto dia útil do mês.</p>
