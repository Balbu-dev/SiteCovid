                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Prefeitura de Maceió publicou, nesta sexta-feira (9), a edição extraordinária do Decreto n° 8.975 e prorrogou as medidas de distanciamento social vigentes até o início da Fase Verde. Entre a determinações, está a autorização para a utilização, a venda de créditos e o recadastramento do Cartão Bem Legal Escolar para os estudantes de clínicas-escolas das Instituições de Ensino Superior (IES) e escolas técnicas situadas em Maceió, desde que as mesmas tenham retornado as suas atividades, que passa a valer a partir da próxima terça-feira (13).</p>
                                <p>A iniciativa leva em consideração a publicação do Decreto Nº 8.965, de 25 de setembro, que autorizou a retomada das aulas práticas em clínicas das instituições de Ensino Superior (IES) e escolas técnicas.</p>
                                <p>O Cartão Bem Legal Escolar para alunos das redes municipal e estadual de ensino, além de escolas técnicas e instituições de ensino superior que não sejam da área da saúde seguem suspensos. O Programa Domingo é Meia também continua suspenso.</p>
                                <p>Além disso, estão mantidos o ponto facultativo presencial e o regime de teletrabalho para os servidores e empregados dos órgãos e entidades da administração pública municipal que exercem funções administrativas internas. Caberá às secretarias municipais e a Procuradoria Geral do Município (PGM) disciplinarem, por meio de portaria, os respectivos atendimentos presenciais ao público, devendo, conforme o caso, designar servidores e colaboradores que deverão atuar presencialmente em tal atendimento.</p>
                                <p>Permanecem paralisadas as atividades educacionais presenciais em todas as escolas da Rede de Ensino Infantil e Fundamental do Município, bem como das instituições de ensino privadas situadas em território municipal até que seja autorizada a retomada pelo Governo Estadual.</p>
                                <p>Entre as atividades liberadas enquanto perdurar a Fase Azul do Plano Estadual de Distanciamento Social Controlado está o funcionamento de lojas ou estabelecimentos de rua, shoppings centers, galerias, centro comerciais e estabelecimentos congêneres - com horário de funcionamento de 10h às 22h-, de salões de beleza e barbearias, templos, igrejas e demais instituições religiosas, com 75% da sua capacidade, além de bares e restaurantes, com 75% da sua capacidade e com horário de atendimento até duas horas.</p>
                                <h2>Uso de máscaras</h2>
                                <p>Segue obrigatório o uso de máscaras sobre o nariz e a boca em locais públicos em locais de uso coletivo, ainda que privados. A determinação vale para todo o território municipal e os maceioenses devem ficar atentos a este item, pois os estabelecimentos devem impedir a entrada e permanência de pessoas que descumprirem o decreto.</p>
                                <h2>Atividades na orla e praças</h2>
                                <p>Até o dia 23 de outubro fica permitida a prática de corrida, caminhada e ciclismo de forma individualizada, o calçadão da orla e praças, com as recomendações de proteção e distanciamento dos decretos anteriores, assim como as visitas aos túmulos de cemitérios. Já a prática de esportes coletivos com contato físico direto, nas orlas, praças, parques, quadras, escolas de esportes e ginásios públicos e privados está proibida até a mesma data.</p>
                                <p>O decreto mantém a autorização do funcionamento de playgrounds e serviços voltados para crianças em estabelecimentos comerciais. Entre outras condições, eles devem fazer o controle de frequência e de acesso aos espaços e equipamentos, além de utilizar apenas 50% da capacidade dos espaços.</p>
                                O banho de mar e uso da praia na faixa arenosa, preferencialmente de forma individualizada, segue liberado, assim como o passeio com animais domésticos ou de estimação, a utilização de parques infantis e mobiliários urbanos esportivos situados na orla e nas praças, treinos funcionais ao ar livre, desde que sem contato físico, e o estacionamento de veículos nos espaços públicos da orla, em vagas intercaladas.
                                <h2>Transportes Públicos</h2>
                                <p>Já a capacidade de passageiros nos transportes públicos urbanos denominada "Bancada", passageiros sentados em sua totalidade da capacidade do veículo, acrescentada da totalidade de passageiros em pé, sem restrições, segue liberada, devendo adotar medidas preventivas já estabelecidas, como janelas abertas e o uso de máscara.</p>
                                <p>Tendo como base os dados fornecidos pela Gerência de Vigilância das Doenças e Agravos Transmissíveis e Não Transmissíveis da Secretaria Municipal de Saúde (SMS) serão intensificadas as fiscalizações em bairros de Maceió que apresentem elevados números de casos confirmados, com a atuação conjunta da Vigilância Sanitária Estadual e Municipal, Polícia Militar, Polícia Civil, Procon e Guarda Municipal.</p>
                                <p>As medidas de controle, prevenção e fiscalização para enfrentamento da Calamidade em Saúde Pública de Importância Internacional decorrente do novo coronavírus (Covid-19), instituídas no âmbito do município de Maceió, poderão ser reavaliadas a qualquer tempo, de acordo com a situação epidemiológica do Município.</p>
