                            <div class="row h-75 text-justify">
                                <p>Os números chamam atenção. 97 milhões de pessoas atendidas em 88 países. Em média, 5.600 caminhões, 30 navios e quase 100 aviões estão na ativa diariamente, entregando alimentos e outros tipos de assistência a quem precisa. Todo esse esforço tem um objetivo: acabar com a fome e alcançar a segurança alimentar até 2030.</p>
                                <p>Esse é o trabalho do Programa Mundial de Alimentação (PMA) da ONU (Organização das Nações Unidas), que ganhou o Nobel da Paz deste ano.</p>
                                <p>Maior organização humanitária em combate a fome, o PMA não visa apenas a distribuição de comida, que ocorre principalmente em situações de emergência, mas sim trabalhar a educação nutricional de famílias em situações de vulnerabilidade. Em 2019, forneceu merenda escolar para mais de 17,3 milhões de crianças em 50 países, geralmente nas áreas mais difíceis de alcançar.</p>
                                <p>A atuação de emergência do órgão acontece ainda em regiões onde pessoas estão fugindo de áreas de conflito. "Garantir que o alimento chegue a quem precisa também ajuda a criar um ambiente de paz e estabilidade. Há uma correlação comprovada entre fome e conflitos. Locais onde há fome ficam suscetíveis a conflitos e guerras. Resolvido o problema da fome, estaremos mais próximos de atingir a paz", diz Daniel Balaban, diretor do Centro de Excelência Contra a Fome do Programa Mundial de Alimentos (WFP, na sigla inglês) da ONU no Brasil.</p>
                                <p>Criado em 1961, a pedido do então presidente dos Estados Unidos Dwight Eisenhower, para atuar em locais com desastres naturais ou em zonas de conflito, a primeira ação do programa da ONU aconteceu um ano depois, após um terremoto no Irã, que matou mais de 12 mil pessoas e deixou centenas de famílias sem abrigo. Na ocasião, o PMA enviou alimento aos sobreviventes. Com a iniciativa se consolidando ano após ano, enfrentou um de seus maiores desafios para combater a fome na Etiópia em 1984, quando arrecadou 2 milhões de toneladas de alimentos. Em 1989, lançou uma megaoperação com um consórcio de agências da ONU e outras instituições ao lado da UNICEF (Fundo das Nações Unidas para a Infância), para liberar 1,5 milhão de toneladas de alimentos para regiões afetadas pela guerra civil no sul do Sudão.</p>
                                <p>Recentemente, além dos esforços para combater a fome e a pobreza durante a pandemia de Covid-19, o Programa Mundial de Alimentação intensificou as ações no Líbano depois da explosão que aconteceu em um terminal do Porto de Beirute, em agosto. Foram enviados suprimentos e 17,5 mil toneladas de farinha de trigo, com objetivo de manter a oferta do produto e baixar o preço do pão na região.</p>
                                <p>Hoje, 820 milhões de pessoas estão passando fome no mundo. Com a pandemia, esse número pode ter um aumento de 270 milhões. Para se manter presente em diversos países pelo mundo, o PMA tem parceria com mais de 1.000 ONGs nacionais e internacionais e trabalha com duas organizações sediadas em Roma, a Organização para a Alimentação e Agricultura das Nações Unidas e o Fundo Internacional para o Desenvolvimento Agrícola. O órgão é inteiramente financiando por doações e conta com 17 mil funcionários em todo o mundo.</p>
                                <h2>Como ajudar</h2>
                                <h3>Centro de Excelência Contra a Fome</h3>
                                <p>As doações para o programa no Brasil podem ser feitas em qualquer valor, via cartão de crédito, boleto bancário ou pagamentos digitais, diretamente no site da instituição.</p>
                                <h3>WFP</h3>
                                <p>Assim como doações de qualquer quantia, a matriz do Programa Mundial de Alimentos da ONU oferece assinaturas mensais para quem quer ajudar a instituição de maneira mais permanente. Também é possível se tornar um voluntário no programa.</p>
