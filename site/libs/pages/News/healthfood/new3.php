                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>A Prefeitura de Juiz de Fora, por meio da Secretaria Municipal de Educação, alerta aos pais e responsáveis pelos alunos da rede municipal de ensino sobre as prestações de contas do benefício do cartão vale-alimentação.</p>
                                <p>Em maio, o G1 mostrou que o benefício, no valor de R$ 50, seria disponibilizado pela Administração durante a pandemia do novo coronavírus para famílias em vulnerabilidade social e econômica, que tenham estudantes matriculados em creches, pré-escolas e escolas municipais.</p>
                                <p>Na ocasião, ficou determinado que a prestação de contas deveria ser efetuada, impreterivelmente, no prazo de 15 dias corridos, a contar da data da liberação dos respectivos créditos.</p>
                                <p>Conforme a Prefeitura, a prestação de contas deve ser feita através do endereço jf.e-beneficio.com, com cópia da nota fiscal, constando no documento o Cadastro de Pessoa Física (CPF) do portador do cartão.</p>
                                <p>No caso de impossibilidade de utilização desta plataforma, o responsável pode ir presencialmente na sede da Secretaria Municipal de Educação, na Avenida Getúlio Vargas, 200, no Centro, entre 12h e 17h, nos dias úteis.</p>
                                <p>Segundo o Executivo, a Administração prorrogou o pagamento do benefício referente à quarta parcela. O valor está liberado desde o dia 2 de outubro.</p>
                                <p>As famílias dos alunos ainda terão direito a receber mais duas parcelas no valor de R$ 50. A última será quitada em novembro, quando termina o termo aditivo do contrato da Prefeitura com a empresa Ticket.</p>
