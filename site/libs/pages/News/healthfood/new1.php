                    <p class="font-weight-bold text-center align-middle display-4">Pandemia de covid-19 ameaça segurança alimentar pelo mundo<p>
                </div>
                <div class="container">
                    <div class="container pt-3 pb-3">
                        <div class="container mb-3">
                            <img src="../../../images/news/pandemia-de-coronavirus-supermercados-amanhecem-lotados-em-vitoria-209185.jpeg" class="rounded mx-auto d-block img-fluid" alt="Saúde Logo" id="logo">
                        </div>
                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Durante anos, o Índice Global da Fome refletiu o progresso global na luta contra a fome. Mas o coronavírus ameaça desfazer essas conquistas, avalia a organização humanitária alemã Welthungerhilfe.Há cinco anos, a Organização das Nações Unidas (ONU) definiu como uma de suas metas erradicar a fome no mundo até 2030. Ou seja: todo ser humano, mesmo nos países mais pobres, deveria ter uma alimentação adequada. Mas e hoje, qual é a situação mundial? E será que estamos no caminho certo para atingir esse objetivo?</p>
                                <p>Em 2015, isso parecia algo ambicioso, mas alcançável. Afinal, a situação alimentar global melhorou muito em apenas alguns anos. Em 2000, o Índice Global da Fome (GHI, na sigla em inglês) atribuiu a todo o planeta uma pontuação de 28,2, o que significa que a situação era grave. Hoje, com uma pontuação de 18,2, a fome é vista como apenas moderada - zero, neste caso, significaria fome nenhuma, enquanto 100 seria a pior pontuação possível.</p>
                                <p>O GHI é calculado com base em quatro indicadores componentes da fome:</p>
                                <p>Desnutrição (parcela da população com ingestão calórica insuficiente)</p>
                                <p>Emaciação infantil (proporção de crianças menores de 5 anos que têm baixo peso em relação à altura - um reflexo de subnutrição aguda)</p>
                                <p>Baixa estatura infantil (proporção de crianças menores de 5 anos que têm baixa estatura para a idade - evidência de desnutrição crônica)</p>
                                <p>Mortalidade infantil (taxa de mortalidade entre crianças menores de 5 anos)</p>
                                <p>Fracasso moral</p>
                                <p>Apesar do progresso, as estatísticas recentes ainda são assustadoras: quase 690 milhões de pessoas no mundo todo sofrem de desnutrição; 144 milhões de crianças têm distúrbio de crescimento; 47 milhões de crianças apresentam emagrecimento extremo e, em 2018, 5,3 milhões de crianças morreram antes de seu quinto aniversário, frequentemente devido à desnutrição.</p>
                                <p>Em seu último relatório, a organização humanitária alemã Welthungerhilfe se refere à fome no mundo como "o maior fracasso moral e ético da nossa geração". Mesmo que a média mundial tenha melhorado, as diferenças entre regiões e países são enormes. A África Subsaariana (27,8) e o sul da Ásia (26,0) são as regiões com os piores índices de fome do mundo.</p>
                                <p>O que está impedindo o progresso no combate a esse problema? Simone Pott, porta-voz da Welthungerhilfe, cita "crises e conflitos, junto com pobreza, desigualdade, sistemas de saúde ruins e as repercussões das mudanças climáticas" como os principais fatores nesta equação.</p>
                                <p>Ela dá o exemplo de Madagascar: "O GHI é maior hoje do que em 2012. Entre os problemas do país, está o aumento da pobreza e da instabilidade política, bem como as consequências das mudanças climáticas." Mas o Congo e a República Centro-Africana são os lanternas do relatório, diz ela, com "conflitos violentos e eventos climáticos extremos retardando qualquer desenvolvimento positivo".</p>
                                <p>Os acertos do Nepal</p>
                                <p>Mas também existem exemplos positivos. Em 2000, a situação em dois países - Camarões e Nepal - era categorizada como "muito grave", mas hoje ambos estão entre as nações com índices moderados de fome.</p>
                                <p>Nos Camarões, a produção econômica per capita mais que dobrou entre 2000 e 2018, passando de 650 para 1.534 dólares, de acordo com dados do Banco Mundial.</p>
                                <p>No caso do Nepal, Simone Pott explica as razões do progresso: "Os investimentos em desenvolvimento econômico reduziram a pobreza. As intervenções no setor de saúde levaram a uma menor taxa de mortalidade infantil e a uma melhor saúde em geral. Mais investimentos na agricultura resultaram em mais segurança alimentar", diz ela.</p>
                                <p>Angola, Etiópia e Serra Leoa também tiveram grandes melhorias desde 2000, e suas pontuações no GHI caíram mais de 25 pontos. Em 2000, os três países ainda estavam na categoria "muito grave", principalmente por causa das guerras civis, que são uma das principais causas da fome e da desnutrição.</p>
                                <p>Remédio pior que a doença?</p>
                                <p>Agora uma grande incógnita entrou na equação: a covid-19 e suas consequências. Nada disso é levado em conta no relatório. Crises econômicas levam a quedas de receita. Para muitos países, isso significa que terão que importar menos alimentos. Segundo estimativas da Organização das Nações Unidas para Agricultura e Alimentação (FAO), isso poderia levar a desnutrição de até 80 milhões de pessoas a mais só nos países com importação líquida de alimentos.</p>
                                <p>Mathias Mogge, secretário-geral da Welthungerhilfe, tem receios semelhantes. "A pandemia e suas consequências econômicas têm o potencial de duplicar o número de pessoas afetadas por crises alimentares agudas", avalia.</p>
                                <p>Mesmo nos países ocidentais, muitas vezes surge a pergunta se as consequências econômicas das medidas adotadas para conter a propagação do coronavírus não seriam piores do que os problemas de saúde causados pelo próprio vírus - ou seja, se o remédio não seria pior do que a doença.</p>
                                <p>Pott acredita que esse é o caso de muitos países do Hemisfério Sul. "O lockdown teve consequências terríveis, especialmente para as milhões de pessoas que trabalham no setor informal", afirma. "De um dia para o outro, elas perderam seus rendimentos, os mercados locais tiveram que fechar, e os pequenos agricultores não puderam mais cultivar seus campos." Não é fácil, portanto, calcular o que é pior em cada país de maneira individual.</p>
                                <p>No que diz respeito à erradicação da fome no mundo até 2030, Pott também não se mostra otimista. "Infelizmente, não estamos no caminho certo", diz ela. "A tendência geral é positiva, mas o progresso é muito lento. Se a situação alimentar se desenvolver da maneira que tem sido observado até agora, 37 países provavelmente não atingirão um nível baixo de fome na escala GHI em 2030. Cerca de 840 milhões de pessoas poderão estar desnutridas - e os efeitos da pandemia de coronavírus ainda não entraram neste cálculo."</p>
