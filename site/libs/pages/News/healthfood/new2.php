                                <p>Na segunda reportagem da série sobre a rede de solidariedade alimentar em curso no DF, a Agência Brasília mostra como a iniciativa – baseada na integração de instituições públicas, com engajamento da sociedade civil – garante o funcionamento de grupos socioassistenciais por meio de compra ou doação de alimentos.</p>
                                <p>Um dos diversos projetos de cunho social em andamento no DF, o Programa de Coleta e Doação de Alimentos (PCDA) tem 126 entidades socioassistenciais em seu cadastro de beneficiários. Conduzida pelas secretarias de Agricultura, Abastecimento e Desenvolvimento Rural (Seagri-DF) e de Desenvolvimento Social (Sedes), a iniciativa assegura a distribuição semanal das chamadas “cestas verdes” por meio do Programa de Aquisição da Produção de Agricultura do Distrito Federal (Papa-DF). Frutas, verduras, legumes e tubérculos, tudo temperado com muita solidariedade, uma receita que garante alimentação saudável de pessoas carentes, com foco em crianças.</p>
                                <p>“É como estamos sobrevivendo, porque com a nossa verba não daria para pagar funcionários, encargos e alimentação” Maria Meire Nascimento, vice-presidente do Lar da Criança.</p>
                                <p>Trata-se de um serviço essencial, pois muitas das entidades contempladas só conseguem manter seu funcionamento justamente devido aos alimentos adquiridos e a elas repassados pelo Governo do Distrito Federal. Além do suporte em si aos grupos assistenciais, o PCDA garante diversidade de alimentos, qualidade nutricional e, sobretudo, segurança alimentar para pessoas em vulnerabilidade social.</p>
                                <p>O Lar da Criança Padre Cícero, em Taguatinga, é uma das entidades cadastradas no PCDA. Há quatro meses a entidade recebe os mantimentos do programa, que passaram a ser distribuídos pelo Banco de Alimentos da Centrais de Abastecimento (Ceasa-DF) em decorrência da crise gerada pelo novo coronavírus.</p>
                                <p>A vice-presidente do Lar da Criança, Maria Meire Nascimento, diz que o serviço é ainda mais fundamental para a alimentação dos assistidos no atual contexto da pandemia de Covid-19, com a consequente diminuição das doações. “É como estamos sobrevivendo, porque com a nossa verba não daria para pagar funcionários, encargos e alimentação”, admite.</p>
                                <h2>Acolhimento</h2>
                                <p>A vice-presidente do Lar da Criança, Maria Meire Nascimento, diz que o serviço é ainda mais fundamental para a alimentação dos assistidos no atual contexto da pandemia de Covid-19, com a consequente diminuição das doações. “É como estamos sobrevivendo, porque com a nossa verba não daria para pagar funcionários, encargos e alimentação”, admite.</p>
                                <p>Os 14 pequenos que moram no lar recebem alimentação e vestuário. Lá eles também praticam atividades lúdicas, recebem atendimento médico e têm acompanhamento de serviço social. Além desse serviço de abrigo, a creche acolhe outras crianças de 2 a 5 anos, o que totaliza 766 menores assistidos.</p>
                                <p>“Somos uma importante rede de proteção às famílias e crianças em situação de vulnerabilidade social” Mayara Noronha Rocha, secretaria de Desenvolvimento Social.</p>
                                <p>Nutricionista do abrigo, Monique Taira Silva lembra que a alimentação na primeira infância requer atenção e qualidade nutricional especiais. “Ficamos surpresos com a qualidade e variedade das frutas, verduras e legumes. Dependendo do carregamento, vêm frutas como maracujá e morango, que costumam ser mais caras”, explica a especialista.</p>
                                <p>As doações do Banco de Alimentos também proporcionaram a diversificação do cardápio dos menores assistidos, em um processo que aproveita até as cascas dos alimentos. Todas as quintas-feiras o lar recolhe cerca de 360 quilos de produtos no banco, o que corresponde a cerca de 120 cestas verdes.</p>
                                <p>Secretária de Desenvolvimento Social do DF, Mayara Noronha Rocha destaca que a estrutura da política distrital de segurança alimentar e nutricional, com destaque para o Cartão Prato Cheio, beneficia diversas organizações da sociedade civil – apenas este programa contempla dezenas de milhares de famílias com insuficiências alimentares e nutricionais.</p>
                                <p>“Somos uma importante rede de proteção às famílias e crianças em situação de vulnerabilidade social”, afirma a secretária, enfatizando a importância das ações decorrentes da parceria entre a Secretaria de Desenvolvimento Social e a sociedade civil.</p>
                                <h2>Ações integradas</h2>
                                <p>A atual gestão do GDF tem como uma de suas principais marcas o trabalho de forma integrada, com o envolvimento do maior número possível de atores governamentais nas mais diversas políticas públicas. Desde maio a Seagri e a Sedes, em parceria com a direção do Banco de Alimentos da Ceasa-DF, atuam na distribuição de cestas verdes para instituições socioassistenciais e famílias em situação de vulnerabilidade devidamente cadastradas.</p>
                                <p>“O acesso ao alimento por meio de compra ou doação às instituições pelas secretarias é fundamental para assegurar a segurança alimentar e nutricional dessas pessoas”, resume a subsecretária de Segurança Alimentar e Nutricional (Subsan), Karla Lisboa.</p>
                                <p>Ela defende que a colaboração entre as pastas na entrega de cestas, como complemento ao programa Prato Cheio, é mais do que uma simples distribuição de alimento. É, muitas vezes, a única fonte de alimentação de muitas famílias.</p>
