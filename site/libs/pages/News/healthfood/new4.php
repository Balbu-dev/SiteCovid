                            <div class="row h-75 text-justify">
                                <p>O Estado do Rio de Janeiro passou a ter legislação própria para o Programa de Aquisição de Alimentos (PAA) a partir da sanção da Lei 8987/2020. O PAA é um dos principais Programas de Segurança Alimentar, que já existe no governo federal desde 2003, e tem duas finalidades básicas: promover o acesso à alimentação e incentivar a agricultura familiar.</p>
                                <p>– Essa Lei é um avanço importantíssimo, pois potencializará o fomento da segurança alimentar nesse momento de crise sanitária causada pela Covid-19 e pela crise socioeconômica – afirmou a secretária de Desenvolvimento Social e Direitos Humanos, Cristiane Lamarão.</p>
                                <p>Para alcançar esses objetivos, o programa compra a produção de agricultores familiares ou empreendedores rurais produzidos em condições orgânicas e agroecológicas. Esses alimentos são doados às entidades de amparo social, equipamentos públicos de alimentação e nutrição (restaurantes populares, bancos de alimentos, cozinhas comunitárias), populações em estado crítico de vulnerabilidade social, hospitais e escolas públicas, presídios e creches estaduais.</p>
                                <p>Graças a doações e produtos adquiridos em Bancos de Alimentos, o projeto social Faixa Preta de Jesus consegue oferecer 3 mil refeições por mês e doar cestas básicas a cada 15 dias para famílias carentes. Com a implementação do PAA estadual, haverá uma nova oportunidade para essas entidades adquirirem os gêneros alimentícios.</p>
                                <p>Já existe no estado um PAA do Governo Federal na modalidade Compra com Doação Simultânea, operado pela CEASA-RJ. Na Central são cadastrados agricultores familiares e feita a recepção dos gêneros alimentícios, que são entregues aos Bancos de Alimentos e, em seguida, para as instituições beneficiadas. Após esse processo, o Governo Federal paga os agricultores a partir das informações que a CEASA-RJ transmite sobre os alimentos adquiridos e quem os produziu.</p>
                                <p>Não há conflito ou concorrência entre as leis estadual e federal. Os recursos virão do Fundo Estadual de Assistência Social, admitindo-se também recursos de auxílios emergenciais federais recebidos pelo Estado e outros instrumentos. A Secretaria de Estado de Desenvolvimento Social e Direitos Humanos é quem determinará o órgão que fará a seleção e a priorização das entidades recebedoras dos alimentos, podendo ser a própria secretaria escolhida para tal tarefa.</p>
                                <p>– Essa alternativa é boa demais. É uma beleza para o produtor rural, porque é uma venda garantida. Você sabe que vai poder produzir e que vai ter para onde vender – afirmou o agricultor familiar de São Francisco de Itabapoana, Jailton Elias Miranda.</p>
                                <p>A Lei ainda precisa de regulamentação. A Câmara Intersecretarias de Segurança Alimentar e Nutricional Sustentável está trabalhando uma proposta de decreto, colhendo contribuições do Conselho Estadual de Segurança Alimentar e Nutricional.</p>
