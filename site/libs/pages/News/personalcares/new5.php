                                <p>A Organização Mundial da Saúde (OMS) decretou nesta semana estado de pandemia em decorrência do coronavírus (COVID-19). O Hospital Pequeno Príncipe, ciente de seu papel de referência em atendimento pediátrico e na proteção de crianças e adolescentes, vem orientar a sociedade a adotar cuidados essenciais a fim de prevenir o risco de contágio ou transmissão de infecções respiratórias, incluindo o coronavírus (COVID-19). É muito importante que cada pessoa aja com responsabilidade, praticando o autocuidado, além de estender os cuidados aos meninos e meninas sob sua responsabilidade.</p>
                                <p>O Pequeno Príncipe também reforça que está tomando todas as medidas necessárias para manter um atendimento seguro e com qualidade para os pacientes internados no Hospital, assim como para os acompanhantes das crianças e dos adolescentes, e os profissionais que atuam na instituição. No Pequeno Príncipe, ainda não há casos confirmados da doença.</p>
                                <h2>Mudança de hábitos é fundamental</h2>
                                <p>O momento é de alerta e as medidas de contenção são imprescindíveis. A mudança de hábitos é fundamental e exige o compromisso de cada cidadão. Assim, a população pode se proteger e também contribuir para a contenção da pandemia.</p>
                                <p>Fique atento às medidas preventivas:</p>
                                <ul>
                                    <li>Utilize álcool 70% com glicerina, que é muito eficaz para a higiene das mãos.</li>
                                    <li>Lave as mãos com água e sabão sempre que estiverem sujas.</li>
                                    <li>Evite tocar a boca, o nariz e os olhos sem antes higienizar as mãos.</li>
                                    <li>Não compartilhe copos, talheres e outros objetos pessoais.</li>
                                    <li>Mantenha os ambientes ventilados. Abra as janelas e portas mesmo em dias frios.</li>
                                    <li>Evite aglomerações e contato com pessoas com sintomas de doenças respiratórias.</li>
                                    <li>Ao tossir ou espirrar, utilize o antebraço para evitar a disseminação do vírus.</li>
                                    <li>Cumprimente as pessoas sem aperto de mão, beijos ou abraços.</li>
                                    <li>Supervisione para que crianças menores, que costumam levar objetos à boca, não compartilhem itens com outras crianças.</li>
                                </ul>
                                <h2>Sobre o uso de máscaras cirúrgicas</h2>
                                <p>Outro ponto importante é em relação às máscaras cirúrgicas, que são utilizadas para prevenir a transmissão de infecções. O uso dessas máscaras deve ser feito apenas por indivíduos que já estão com alguma doença respiratória (coriza, tosse e/ou espirro) e cuidadores ou profissionais de saúde que estão envolvidos diretamente com o doente.</p>
