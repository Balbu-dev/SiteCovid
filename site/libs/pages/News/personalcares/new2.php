                                <p>Quando se fala dos hábitos de higiene pessoal, logo vem à sua mente tomar banho ou escovar os dentes? Sem dúvida essas atitudes são importantes para a saúde, mas para afastar o risco de doenças, principalmente em tempos de pandemia da Covid-19, vale a pena incorporar alguns outros cuidados ao seu dia a dia.</p>
                                <h2>1. Lave sempre as mãos</h2>
                                <p>Você deve estar ouvindo isso com frequência, e não é à toa. Lavar as mãos é a melhor maneira de eliminar microrganismos causadores de diversas doenças. Pense bem: durante o dia, tocamos em diversas superfícies contaminadas, muitas vezes sem nem notar. E a mão, quente e úmida, é um ambiente favorável para o desenvolvimento, proliferação e transmissão de infecções virais, bacterianas e parasitárias. Por isso, se ela não for higienizada da forma correta e esses agentes infecciosos entrarem em contato com as mucosas, seja coçando os olhos, o nariz ou até passando os dedos pelos lábios, há o risco de desenvolver a Covid-19, como também outros tipos de doenças, como resfriados, gripes, hepatite A, sarampo, rubéola, conjuntivite, caxumba, parasitoses intestinais, entre outras. O ideal é lavar as mãos com água e sabonete antibacteriano – que elimina com eficácia as bactérias e os vírus – principalmente antes e depois de ir ao banheiro, antes e depois das refeições, ao manusear dinheiro, depois de cumprimentar pessoas e ao chegar em casa.</p>
                                <h2>2. Capriche no banho</h2>
                                <p>Nada como uma ducha gostosa para relaxar e aliviar o estresse, certo? Mas, além desse efeito, o banho é um hábito de higiene fundamental para eliminar as sujeiras e os microrganismos acumulados na superfície da pele durante o dia. Na hora de escolher seus produtos de higiene pessoal, opte por um sabonete antibacteriano, que não faz mal nenhum à saúde, muito pelo contrário, ele oferece proteção extra, já que elimina os vírus e bactérias causadores de infecções e doenças. Lave com capricho cada parte do corpo, dê atenção principalmente ao couro cabeludo (para evitar o surgimento de dermatite e alergias), à barba (para impedir a proliferação de germes) e entre os dedos (para se proteger da micose). Lembre-se, ainda, de secar muito bem a região, que, por ser quente e úmida, torna-se o ambiente perfeito para o desenvolvimento de fungos.</p>
                                <h2>3. Unhas bem aparadas</h2>
                                <p>Procure mantê-las limpas e bem cortadas. Esse cuidado evita o acúmulo de sujeirinhas bem como de germes embaixo delas. E atenção: se você costuma roer as unhas, tente se livrar desse hábito, que destrói as cutículas e a pele, partes importantes para a proteção contra bactérias. Além disso, ao levar as unhas à boca (e muitas vezes até engolir), você pode ser infectado por microrganismos, como vírus e bactérias, e acabar doente. Nunca se esqueça: olhos, boca e nariz são porta de entrada de germes causadores de diversas doenças.</p>
                                <h2>4. Cubra a boca ao tossir e espirrar</h2>
                                <p>Se sentir necessidade de tossir ou espirrar, lembre-se de cobrir a boca com a parte interna do braço. Essa regra é fundamental, e vem sido constantemente lembrada, desde a chegada da Covid-19, porque a transmissão da maioria das doenças virais ocorre através de secreções que liberamos quando tossimos, espirramos ou falamos. Como essas gotículas respiratórias são expelidas a curta distância, é importante que as pessoas mantenham a distância de 2 metros umas das outras para impedir a propagação.</p>
                                <h2>5. Kit de higiene sempre à mão</h2>
                                <p>Tenha uma embalagem de álcool gel sempre por perto, para assegurar a higienização quando não houver água e sabonete. O produto é eficiente na eliminação dos germes causadores de doenças e pode ser levado na bolsa ou guardado na gaveta do escritório – uma ótima solução para manter a higiene pessoal também no trabalho. Na hora da compra, verifique se ele contém pelo menos 70% de álcool gel. Ao aplicá-lo, garanta cobertura em todas as partes das mãos e esfregue-as por 20 a 30 segundos, entre os dedos, no dorso e nas palmas, até que fiquem secas.</p>
                                <p>Agora é só incorporar esses hábitos de higiene à sua rotina diária, para você e os outros ficarem mais protegidos e saudáveis.</p>
