                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Ainda restam muitas dúvidas sobre os cuidados para evitar a contaminação pelo novo coronavírus, que causa a Covid-19. Embora as principais recomendações continuem sendo as do isolamento social, para quem pode ficar em casa, e da higienização constante das mãos, além do uso da máscara nas saídas essenciais, há outras medidas que ajudam a evitar a doença.</p>
                                <p>São cuidados pessoais que incluem a correta limpeza de objetos de uso diário, como óculos e lentes de contato; a forma certa de manter a barba, o cabelo e as unhas; além das principais precauções com a alimentação e a hidratação.</p>
                                <p>Segundo o infectologista Fernando Chapermann, professor da PUC-RJ, ficar atento a esses detalhes é importante porque o vírus pode sobreviver por horas em diferentes superfícies:</p>
                                <p>— A lavagem frequente das mãos, com água e sabão, é fundamental, pois o vírus não penetra na pele, mas fica ali. Quando colocamos o dedo na boca, no olho ou no nariz, o que fazemos constantemente sem perceber, ele acaba contaminando — afirma.</p>
                                <p>Uma das principais precauções, segundo os especialistas, é manter as unhas cortadas, não só porque elas podem funcionar como área de depósito do vírus, mas também impede a higienização correta das mãos.</p>
                                <p>— Outro cuidado importante par a os homens é, pelo menos neste momento, retirar a barba ou mantê-la curta. A região também pode servir de acúmulo para o vírus, além de dificultar a higienização do rosto, estimular o toque na face e dificultar a correta utilização das máscaras — alerta Kirley de Souza, enfermeira especializada em controle de infecção hospitalar.</p>
                                <p>Outro objeto que costuma estimular o toque no rosto são os óculos. Embora ele sirva como uma certa barreira de contato entre o vírus e os olhos, os cuidados precisam ser reforçados ao manuseá-los.</p>
                                <p>— Qualquer objeto que a gente toca muito, como os óculos, mas também celular, caneta e escova de cabelo, por exemplo, precisa ser higienizado todas as vezes que for manuseado. Intensificar ainda mais a lavagem das mãos é recomendado — ensina Souza.</p>
                                <p>Outro cuidado é com a pele: o uso de hidratante corporal diário previne rachaduras e feridas, que podem servir como entrada para o novo coronavírus.</p>
                                <h2>PREVINA-SE</h2>
                                <h3>Mantenha as unhas curtas</h3>
                                <p>Unhas compridas dificultam a higienização correta das mãos, pois servem como áreas para acúmulo de micro-oganismos, inclusive do novo coronavírus. Neste momento de quarentena, o ideal é evitar fazer as unhas, para não sair de casa, não receber pessoas e, no caso de tentar fazer por conta própria, prevenir contra possíveis cortes e feridas. E também não use acrigel.</p>
                                <h3>Na cabeça</h3>
                                <p>Os cabelos devem ser lavados com mais frequência. Também é importante deixá-los sempre presos, não apenas para diminuir a superfície de exposição, mas para evitar passar a mão nele ou mesmo na face.</p>
                                <h3>Barbudos</h3>
                                <p>O ideal é que os homens abdiquem da barba, por enquanto. Ela pode impedir a correta higienização do rosto e servir de repositório de micro-organismos, além de estimular o toque das mãos na face. A barba também pode atrapalhar no correto ajuste da máscara.</p>
                                <h3>Hidratação e alimentação</h3>
                                <p>Beber água aumenta a imunidade e a saúde da pele, assim como hidratantes, que evitam rachaduras e fissuras, que podem ser porta de entrada para microrganismos. Na alimentação, opte por fibras, proteínas e vitaminas, como a A, que ajudam na cicatrização. Bebidas alcoólicas e excesso de carboidratos podem facilitar processos inflamatórios.</p>
                                <h3>Imunidade em dia</h3>
                                <p>Além de uma alimentação repleta de fibras, que fortalece as defesas do corpo, manter uma vida ativa, com a prática de exercícios físicos, mesmo em casa, fortalece a imunidade, o que é fundamental no combate ao coronavírus. Também é muito importante para a saúde mental em tempos de isolamento social, para quem pode ficar em casa.</p>
                                <h3>Lentes de contato</h3>
                                <p>Não é recomendado que se abandone o uso, mas os cuidados no manuseio e limpeza das mãos precisam ser redobrados, já que, ao colocá-las no olho, há contato direto com o globo ocular. Higienizá-las sempre com o removedor de proteína, que não pode ser substituído. Mesmo sem usá-las, é preciso lavá-las ao menos uma vez ao dia, limpando também a caixinha. Não use as lentes por mais de 12h seguidas e mantenha os olhos hidratados.</p>
                                <h3>Óculos</h3>
                                <p>Óculos podem funcionar como barreira de contato entre o vírus e o olho, mas eles também demandam atenção. É comum manuseá-los durante o uso para ajeitá-lo no rosto ou desembaçá-lo. Lavar as mãos antes de encostar e mantê-los sempre limpos são cuidados fundamentais.</p>
                                <h3>Cortes</h3>
                                <p>Se estiver com alguma ferida, o ideal é deixá-la sempre fechada, com esparadrapo ou outro produto descartável, para evitar que funcione como porta de entrada para micro-organismos e também para o vírus. É imprescindível fazer a correta higienização das mãos antes e depois de tocá-las, além de manter as feridas sempre limpas.</p>
                                <h3>Objetos tocados pelas mãos</h3>
                                <p>Bengala, aparelho de audição, medidor de pressão e demais objetos do dia a dia que são manuseados precisam ser higienizados com água e sabão, álcool 70% ou água sanitária - e com mais frequência do que o normal. Pessoas que precisam desses objetos no cotidiano também devem se lembrar de lavar as mãos com ainda mais regularidade.</p>
                                <h3>No sono</h3>
                                <p>Os aparelhos usados por quem tem dificuldade de respirar, como apneia, precisam ser higienizados antes e assim que forem usados, sempre de acordo com a recomendação do fabricante. Eles também precisam ser armazenados em local apropriado, em recipientes higienizáveis ou laváveis.</p>
                                <h3>Objetos pessoais não podem ser compartilhados</h3>
                                <p>Canetas, mouses, teclado, livros e celulares não devem ser compartilhados. Sempre que for utilizá-los, lavar as mãos.</p>
                                <h3>Cartão, cuidados extras</h3>
                                <p>Higienize as mãos com água e sabão antes e depois de usar o seu cartão, sua carteira de dinheiro e a maquininha de pagamento, já que ela provavelmente passou por muitos locais.</p>
