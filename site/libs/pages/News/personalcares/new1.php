                    <p class="font-weight-bold text-center align-middle display-4">Saiba quais são os cuidados pessoais necessários para evitar o contágio pela Covid-19<p>
                </div>
                <div class="container">
                    <div class="container pt-3 pb-3">
                        <div class="container mb-3">
                            <img src="../../../images/news/xPequenos-gestos-contra-o-coronavirus.jpg.pagespeed.ic.CKOkT8GJ85.jpg" class="rounded mx-auto d-block img-fluid" alt="Saúde Logo" id="logo">
