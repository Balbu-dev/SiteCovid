                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Estamos vivendo um período muito difícil em todos os países. Uma pandemia sem precedentes no mundo contemporâneo que mudou a forma que nos comportamos e como cuidamos dos nossos hábitos de higiene.</p>
                                <p>Precisamos estar atentos para passar por esse período com o corpo e a mente sã. Para isso, precisamos estar atentos aos temas prevenção e higiene. É preciso se preocupar com a própria saúde e das pessoas que convivemos, ter bons hábitos de higiene e entender que a limpeza dos ambientes é a extensão dos nossos hábitos de higiene pessoal. Que se não houver um ambiente limpo, nossa saúde e a dos outros pode ser prejudicada. Vamos juntos entender melhor o que fazer para nos prevenir.</p>
                                <h2>O que é Saúde?</h2>
                                <p>Segundo a OMS - Organização Mundial da Saúde - “Saúde é o estado de completo bem-estar físico, mental e social, e não só a ausência de doença”.</p>
                                <p>Essa definição nunca foi tão esclarecedora, pois sabemos de casos assintomáticos, onde as pessoas estão contaminadas pelo Covid-19 e, no entanto, não sentem qualquer mal-estar ou nenhum de seus sintomas, mas continuam disseminando silenciosamente o vírus para outras pessoas ao seu redor. Além disso a saúde mental e social de todos está sob muita tensão, pois quem precisa sair diariamente de casa para trabalhar, sente o medo de se expor à doença. Já quem está isolado em casa sente muita falta da sua rotina, das pessoas e de tudo o que convivia. E como, em ambos os casos, todos devem manter um distanciamento social para se resguardar de qualquer possibilidade de contágio, estamos vivendo todos sob muita pressão sem o apoio de um simples e acolhedor abraço.</p>
                                <h2>Época de resfriados</h2>
                                <p>Para agravar mais ainda a situação, no Estado de São Paulo, estamos em uma época do ano em que a temperatura num único dia varia bastante e ficamos suscetíveis a gripes e resfriados, que têm sintomas muito parecidos com os casos mais leves da Covid-19. Por essa razão, é preciso redobrar os cuidados e evitar contato direto, como beijos, abraços, apertos de mãos, mantendo uma distância mínima de 1,5m de outra pessoa, que é a distância segura para que o vírus não seja transportado pela respiração ou fala. Se houver alguém tossindo a distância deve ser de 2m e é necessário aumentar o distanciamento para 6m de quem está espirrando.</p>
                                <p>Em casa, na rua ou no trabalho, ao espirrar ou tossir é fundamental usar lenços descartáveis e jogá-los em lixeiras com tampa. Se não tiver um lenço à disposição, cobrir a boca e o nariz com o antebraço ao tossir ou espirrar ajuda a brecar o lançamento de vírus no ar. Após usar o antebraço, lavar com água e sabão assim que possível.</p>
                                <h2>Água e sabão são os melhores aliados para manter a higiene</h2>
                                <p>Quando estiver na rua ou no ambiente de trabalho, leve álcool-gel e higienize as mãos sempre que tocar superfícies de uso coletivo, como corrimão de escadas, trincos de portas etc. Se houver onde lavar as mãos com água e sabão, faça uso com mais frequência. Mas lavar as mãos rapidinho não adianta nada! Precisa seguir o seguinte passo a passo contando até 10 em cada etapa:</p>
                                <ul>
                                    <li>Com as mãos úmidas, coloque um pouco de sabonete e esfregue as palmas das mãos uma na outra;</li>
                                    <li>Entrelace os dedos para lavar cada um deles;</li>
                                    <li>Esfregue a parte de trás das mãos;</li>
                                    <li>Lave entre os polegares;</li>
                                    <li>Lave atrás dos dedos;</li>
                                    <li>Esfregue as unhas na palma das mãos;</li>
                                    <li>Esfregue os pulsos;</li>
                                    <li>Seque bem as mãos com uma toalha limpa, de preferência descartável.</li>
                                </ul>
                                <h2>Higiene ao entrar em casa</h2>
                                <p>Para quem precisa sair diariamente ao trabalho ou sair de casa para comprar itens essenciais como alimentos ou remédios é necessário criar uma rotina de higienização para não levar o perigo da contaminação para o lar.</p>
                                <p>Ao chegar em casa, limpe a maçaneta, as chaves, o puxador da porta do elevador, corrimão das escadas etc., utilizando um lenço descartável borrifado com água e gotas de detergente.</p>
                                <p>Outra dica de prevenção é entrar em casa sem os sapatos. Os calçados podem trazer o vírus para dentro do lar. O melhor, nesse momento, é separar um par para sair de casa e mantê-lo próximo à porta. Se não for possível, utilizar apenas um calçado, ao entrar em casa, carregue-os nas mãos até a lavanderia e limpe-os bem com água e gotas de xampu ou detergente, e só depois guarde-o com os outros sapatos.</p>
                                <p>Bolsas e acessórios também devem receber atenção e limpeza necessária, pois podem encostar em muita coisa até chegar em casa. Use um pano com álcool 70 ou água com gotas de detergente ou xampu. Lembre-se de pendurar bolsas em um local adequado para não contaminar sofás, mesas e cadeiras.</p>
                                <p>Já as roupas usadas fora de casa devem ir direto para o cesto de peças sujas, ou ainda colocadas na máquina de lavar até acumular uma boa quantidade para lavar todas as peças de uma só vez.</p>
                                <p>O passo seguinte, ao chegar da rua, é tomar um bom banho e só depois disso se relacionar com as outras pessoas da residência. Antes do banho, claro, evite colocar as mãos no rosto ou mesmo nas paredes ou móveis da casa.</p>
                                <h2>Higienização dos alimentos</h2>
                                <p>As embalagens dos alimentos comprados devem ser limpas antes de guardadas. Caixas de leite, latas de conservas, potes, vidros, garrafas, sacos de alimentos perecíveis ou não, devem ter suas superfícies limpas com pano úmido de água com gotas de detergente. Depois é só secar com um pano seco e limpo e guarda-los normalmente.</p>
                                <p>Essa limpeza é importante, pois esses produtos passam pelas mãos dos funcionários das lojas, de clientes que pegam as embalagens e depois não levam. Em todos estes processos, as embalagens podem ser contaminadas e levar o vírus para casa.</p>
                                <p>Para lavar as verduras e frutas utilizar água corrente e, depois, colocá-los em solução de hipoclorito de sódio, conforme instruções da embalagem, ou mesmo em solução de 1 colher de água sanitária, própria para desinfetar alimentos, em 1 litro de água.  Em seguida, enxaguar novamente para tirar o excesso do produto.</p>
                                <h2>Limpeza e desinfecção de aparelhos eletrônicos</h2>
                                <p>Em casa ou no trabalho utilizamos, a todo momento, aparelhos eletrônicos. Para desinfetar celulares, controle remoto, notebooks, teclados de computador e qualquer outro aparelho eletrônico, borrifar álcool isopropílico em um lenço de papel e só depois passar no aparelho. O álcool isopropílico pode ser encontrado em lojas de aparelhos eletrônicos. Outros tipos de álcool contêm água que prejudica esses aparelhos.</p>
                                <h2>Rotina de limpeza</h2>
                                <p>E atenção, todas essas precauções só valem para quando se chega da rua.  Caso não esteja saindo, não tem motivo para ficar passando álcool em tudo e em todo momento. Limpar a casa uma vez ao dia já é o suficiente. Tirar o pó dos móveis, varrer o chão e utilizar um pano úmido utilizando um desinfetante comum ou água sanitária, respeitando as instruções de diluição presentes na embalagem, já deixa a casa limpa e desinfetada.</p>
                                <h2>Deixe o sol entrar</h2>
                                <p>Outro fator importante para a saúde do ambiente é deixar as janelas abertas para favorecer a circulação e renovação do ar. Sempre que possível, deixar o sol entrar para aproveitar seu calor e sua iluminação natural que traz frescor, bem-estar e esperança de tempos mais leves.</p>
