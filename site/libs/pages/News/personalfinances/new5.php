                                <p>E o que o mundo animal tem a ver com isso? Entre tanta informação, logo se supôs que, se o coronavírus se originou de animais na China, por que, então, cães e gatos, tão próximos do ser humano, não poderiam adoecer e participar da transmissão do agente infeccioso? Bastou uma hipótese não confirmada para o pânico se generalizar.</p>
                                <p>Mas, mesmo antes de se ter uma resposta acadêmica sobre essa possibilidade, uma simples observação nos ajudaria a entender melhor as coisas. Se cães e gatos adoecessem com a Covid-19, não seria óbvio que hospitais veterinários no mundo todo — principalmente na China e na Europa e, mais recentemente, nos Estados Unidos — estivessem abarrotados de pets internados? Só essa observação nos ajudaria a imaginar que os animais de estimação não enfrentam uma doença tão grave como os seres humanos.</p>
                                <p>Com o desenrolar da pandemia, porém, sugiram relatos isolados de quatro cães e um gato na China e outro felino na Bélgica que testaram positivos para o Sars-CoV-2. Todos tiveram contato com o vírus de tutores que também testaram positivo. Como os veterinários e cientistas enxergam isso? Ora, que os pets contraíram o vírus simplesmente pela convivência com donos infectados.</p>
                                <p>Mas adquirir a doença ou mesmo passar a transmitir o patógeno… Aí já é outro negócio. Primeiro porque nenhum desses animais apresentou sintomas clássicos de Covid-19. No entanto, em função dessas observações, duas entidades veterinárias internacionais de respeito (a WSAVA e a AVMA) têm sido claras em suas recomendações: pessoas com o diagnóstico do novo coronavírus devem evitar manusear seus pets sem proteção e, de preferência, devem evitar o contato com eles.</p>
                                <p>A precaução se faz necessária porque estamos diante de uma doença nova, com meses de evolução pelo mundo. Até que todas as evidências sejam coletadas, convém proteger os animais de estimação da exposição ao vírus. O fato de alguns poucos bichos, em meio a tantos humanos, testarem positivo indica que pode haver contato mas não há sintomas nem complicações como as observadas em humanos.</p>
                                <p>E o principal: em todos esses casos não se viu transmissão dos pets para os tutores. E, sim, o contrário!</p>
