                                <h2>Ansiedade</h2>
                                <p>O enorme fluxo de notícias diárias sobre a pandemia gera ansiedade, fazendo com que surja o sentimento de urgência para a compra de diversos itens que possam vir a ser úteis como no caso da higiene pessoal e também de outros tipos de compras oriundas de um impulso movido pela ansiedade, aumentando os gastos desnecessariamente. É preciso lembrar que tudo na vida passa. Não se deixe levar pelo fluxo das notícias e priorize a busca de informações a partir de fontes confiáveis como Agência Nacional de Saúde e indivíduos especializados na área. Procure adquirir apenas o essencial, a máscara por exemplo é recomendada apenas para quem foi infectado ou que apresenta sintomas, já o álcool gel recomenda-se como segunda opção quando não há como lavar as mãos com água e sabão. Perceba que não há, de fato, grande necessidade de gastos. As instituições orientaram que não haverá desabastecimento, não sendo preciso fazer grandes estoques. É preciso encarar o momento com seriedade e lucidez, dessa forma é possível manter o controle do recursos financeiros.</p>
                                <h2>Medicamentos</h2>
                                <p>Nossa cultura tem por hábito a automedicação, além de perigosa, pode também gerar altos custos e até mesmo ter efeitos contrários ao desejado. Não gaste dinheiro com remédios sem orientação médica. E nos casos de necessidade, dê preferência às opções genéricas que são mais econômicas. Esse é um ponto importante de organização financeira independente da pandemia. </p>
                                <h2>Tempo</h2>
                                <p>A Organização Mundial da Saúde (OMS) recomenda o isolamento para que seja possível controlar e reduzir a expansão do vírus. Muitas pessoas já têm ficado em casa e tido mais disponibilidade de tempo. Esse pode ser um momento oportuno para estudar e conhecer mais sobre finanças pessoais, educação financeira e como fazer investimentos. Aqui na Plano temos bons artigos para você conhecer mais sobre esses temas. Diante das adversidades temos a oportunidade de evoluir. Um ponto importante são compras online neste período de reclusão, o uso do cartão de crédito nas compras online pode gerar dívidas desnecessárias. Procure aproveitar o tempo em atividades interativas como estudos, exercícios, tocar um instrumento musical e afins. Essas são atividades salutares e que não demandam altos gastos, permitindo maior economia.</p>
                                <h2>Hábitos</h2>
                                <p>O ser humano é essencialmente aquilo que ele faz repetidamente e essa ideia vem desde de Aristóteles, mesmo assim, sabemos que é preciso muita disciplina para mudar nossos hábitos. Por vezes, são pequenos detalhes que minam nosso rendimento. Você sabe dizer com precisão para onde vai o seu dinheiro? Ou sabe quais são seus gastos recorrentes no cotidiano? Em situações como a que estamos vivendo, nossos hábitos mais enraizados são postos em xeque. Escolas e universidades parando, trabalhos em home-office, eventos da cidade cancelados e até mesmo o transporte pode ser afetado. Toda a vida muda e podemos aproveitar para aprendermos mais sobre nós mesmos e saber onde agir para conquistar boas práticas na nossa vida em geral e no trato com nosso dinheiro e cartão de crédito.</p>
