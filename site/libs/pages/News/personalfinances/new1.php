                            <div class="row h-75 text-justify">
                                <p> O surto do novo coronavírus (Covid-19) revelou-se um inimigo complexo, com impacto significativo nos rendimentos e condições de vida de muitas pessoas. Trata-se de uma pandemia que assusta, assola e ainda provocará muitos danos nas economias do mundo todo — e já se mostra capaz de enfraquecer alguns dos países mais ricos por conta da necessidade do isolamento ou distanciamento social.</p>
                                <p> O PIB mundial deverá sofrer desaceleração e impacto negativo no desenvolvimento econômico geral, segundo um estudo da Fundação Getulio Vargas (FGV) em parceria com o instituto econômico suíço KOF da ETG Zurique. A pesquisa indica ainda que o impacto da pandemia sobre a atividade global é "sem precedentes", tanto em intensidade quanto em velocidade.</p>
                                <p> Em um cenário como este pode ser difícil para pessoas vislumbrarem soluções de curto prazo para problemas de natureza tão sensível como ter sua renda afetada frente a contas que não param (e provavelmente não pararão) de chegar. Mas há algumas dicas que podem ajudar a atravessar este momento delicado com um pouco mais de tranquilidade.</p>
                                <h2>ORGANIZE SUA VIDA FINANCEIRA</h2>
                                <p> Não é segredo que controle e planejamento das finanças pessoais fazem muita diferença positiva sobre como administramos nossas vidas, evitando surpresas desagradáveis que podem lhe privar até mesmo de gastos de consumo essencial. Então é importantíssimo organizar sua vida financeira em um caderno, planilha, aplicativo ou qualquer outro meio que lhe permita entender com precisão quais são suas receitas (o dinheiro que entra) e despesas (o dinheiro que sai). Controle e planejamento financeiro não são importantes apenas em momentos de crise, mas se tornam imprescindíveis quando o futuro está repleto de incertezas.</p>
                                <p> Outro passo importante é separar as contas fixas e o consumo indispensável dos gastos supérfluos ou não tão necessários neste momento: estes dois últimos são os primeiros a serem evitados para gerar economia. Fica mais fácil definir o que é imprescindível através de uma conversa franca com todos os integrantes da família.</p>
                                <h2>REPENSE HÁBITOS DE CONSUMO</h2>
                                <p> Se uma ou mais pessoas de sua família perdeu renda e você se tornou o porto seguro, sendo o único com renda fixa neste instante, é importantíssimo repactuar os hábitos de consumo em seu lar para não gerar um peso ainda maior sobre a única fonte de renda – e evitar endividamento.</p>
                                <p> Muitas pessoas já perceberam a necessidade de cortar os excessos pois o estilo de vida não cabe mais no orçamento. O começo de tudo é organizar-se evitando desperdício de energia, gás e água, modificando ou reduzindo hábitos como utilizar TV por assinatura, aplicativos pagos e outros.</p>
                                <h2>SEJA CRIATIVO COM RENDA EXTRA</h2>
                                <p> É importante tentar ser criativo e verificar outras possibilidades de renda, seja em atividade privada, como trabalho pela internet, ou recorrendo a bolsas sociais do governo e até mesmo um eventual saque de FGTS.</p>
                                <p> Caso possua reservas financeiras como investimentos ou a tradicional poupança e se desequilibre financeiramente, dê preferência a utilizá-los em vez de limites do cheque especial ou rotativo do cartão de crédito. Os juros pagos nestas operações são muito maiores que os recebidos nos investimentos. Tente negociar com fornecedores de serviços e busque alternativas baratas para se financiar agora.</p>
                                <h2>APOIE O PEQUENO EMPRESÁRIO</h2>
                                <p> Quando for consumir o necessário, dê preferência aos micros e pequenos empresários: eles são os primeiros atingidos nesta crise e com certeza têm menos capacidade de caixa para passar por esta fase. Dados da Associação Brasileira dos Lojistas Satélites (Ablos) mostram que os pequenos lojistas estão sendo sufocados pela crise atual. A entidade ressalta que é muito importante que os consumidores prestigiem as lojas menores para a sobrevivência do segmento.</p>
                                <p> Lembre-se também que os pequenos empresários são os maiores empregadores do país: ajudando-os você auxiliará mais pessoas, mesmo que indiretamente. Em resumo é necessário planejar-se, trabalhar em um acordo familiar, dar preferência ao consumo essencial, não usar limites de cheque especial ou cartão e servir-se de recursos existentes. Estas são as chaves para passar por este momento com mais tranquilidade.</p>
