                            <div class="row h-75 text-justify">
                                <p>Com o avanço do novo coronavírus pelo Brasil, surgem preocupações e dúvidas na população em diversos assuntos. Um deles é em relação à Covid-19 e os animais de estimação. Ainda não há evidências de transmissão da doença entre humanos e animais, segundo o Conselho Regional de Medicina Veterinária de Minas Gerais (CRMV-MG), mas é necessário ficar atento a certos cuidados durante a pandemia.</p>
                                <p>Há coronavírus específico que acomete cães e gatos, como explica a médica veterinária, Patrícia Zanini. “Até onde nós sabemos, não existe contaminação entre humanos e animais, justamente porque o vírus acomete espécies específicas. O coronavírus canino infecta cães na forma de gastroenterite e o coronavírus felino infecta os gatos causando uma peritonite infecciosa”, afirma.</p>
                                <p>Dessa forma, em relação à Covid-19 que vem se disseminando no Brasil, não estão proibidos os passeios com o animalzinho pelas ruas, porém recomenda-se que seja algo breve.</p>
                                <p>“A gente recomenda que seja um passeio rápido. O tempo suficiente para ele fazer as necessidades fisiológicas rapidamente, evitando horários com aglomeração, parques e praças. Voltando para casa, claro, é interessante que se faça a higienização das patinhas, com um lencinho umedecido com álcool em gel 70%, por exemplo”, orienta a médica veterinária, Danielle Dourado Maia.</p>
                                <p>Ainda segundo ela, a pelagem do animal pode carregar o novo coronavírus, caso ele tenha entrado em contato com uma pessoa contaminada através de secreções e partículas virais. Nessa situação, é recomendável que o dono dê um banho com shampoo ação detergente no pet.</p>
                                <p>Mas, mesmo com as orientações de prevenção, há aqueles que preferem não expor muito o bichinho, como é o caso da estudante Júlia Werdan.</p>
                                <p>“Com a pandemia, eu parei de sair com o Puppy na rua. O único contato que ele tem é com a menina do pet shop e a veterinária para as vacinas. Mas ele nem pisa na rua, vamos de carro e quando chega no local vou com ele no colo. A veterinária aconselhou sempre limpar as patinhas depois que voltar na rua com um sabão específico, mas prefiro não arriscar”, pondera.</p>
                                <p>Situação semelhante também com a analista de sistemas, Cristina Abrantes. Ela é tutora da Chanel e tem evitado de realizar os passeios na rua.</p>
                                <p>“Desde que a pandemia começou, eu tenho evitado sair com a Chanel na rua. Parei de levar no pet shop que costumava e passei a dar banhos em casa. Agora que vim para Timóteo, pedi a uma pessoa que só atende em casa para dar banho”, conta.</p>
                                <h2>Tutores contaminados com a Covid-19</h1>
                                <p>Inevitavelmente, com o avanço da doença, pode ser comum encontrar animais de estimação no mesmo espaço em que tenha um paciente infectado com o novo coronavírus. Ainda conforme a veterinária Danielle Dourado, as orientações de prevenção nesse caso não são diferentes das que são adotadas entre humanos.</p>
                                <p>“A recomendação é o distanciamento social. Da mesma forma que uma pessoa que está em quarentena numa casa que mora outras pessoas deve ficar isolado, resguardado, a mesma coisa ela vai fazer em relação aos animais. Ela não deve se aproximar no animal, não vai tocar, não vai beijar e nem adular. Caso seja só ela e o animal na residência, os cuidados com o pet devem ser incumbidos a uma outra pessoa, que não seja o tutor contaminado”, explica.</p>
                                <h2>'Passeio' dos gatos</h1>
                                <p>Não é segredo que os felinos tem o costume de sair de casa, dar longos passeios nas ruas ou telhados, e depois voltar. Entretanto, nessa pandemia, essa prática pode ser arriscada, visto que os gatos podem ir a lugares ou ter contatos com pessoas infectadas com a Covid-19.</p>
                                <p>Como é complicado ficar vigilante o tempo todo com o animal, é recomendável a instalação de telas na residência, para evitar o “passeio” do felino.</p>
                                <p>“A orientação seria telar. Em caso de casas, em que o muro é mais baixo, pode se fazer aquelas telas de muro, como se elevasse o tamanho da estrutura. É como se fosse aquelas telas presentes em campos de futebol. O muro fica bem mais alto e, lá em cima, ela volta para o interior de maneira que se o gato tentar ainda escalar, ele encontra com ela virada para dentro. É uma tela específica para delimitar a saída dos gatos”, informou Danielle.</p>
                                <p>Portanto, conforme a veterinária, caso o tutor não consiga evitar que o gato saia, o ideal é sempre estar fazendo uma higienização mais intensa nesse animal, por conta de não saber com quem ele pode encontrar no caminho.</p>
