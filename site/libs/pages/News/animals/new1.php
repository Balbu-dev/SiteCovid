                            <div class="row h-75 text-justify">
                                <p>Após o episódio em que um cão de nove anos foi sacrificado na Carolina do Sul por apresentar diagnóstico de coronavírus, o tema “COVID-19 em animais domésticos” têm sido bastante discutido. Entre outros questionamentos, pessoas têm dúvidas se os pets podem transmitir a doença para humanos, se qualquer animal doméstico é capaz de contraí-la e se os donos devem tomar cuidados com seus animais durante a quarentena.</p>
                                <p>O médico veterinário Bruno Antunes, diretor do curso de medicina veterinária e agronomia do Una Linha Verde, explicou ao Estado de Minas que tudo indica que os animais não possam transmitir coronavírus aos humanos. “Esse vírus é específico de humanos e existem grandes possibilidades de sua sobrevivência dentro de um animal, assim como a disseminação para outros hospedeiros, serem extremamente baixas”, esclareceu.</p>
                                <p>“O causador da COVID-19 é um vírus específico de humanos, temos outros coronavírus que são causadores de doença em animais, mas que não são em humanos”, informou Bruno.</p>
                                <p>O mestre e doutor em Imunologia e Doenças Infeciosas explicou que os animais identificados, até o momento, com COVID-19 estavam em casas em que os donos foram diagnosticados com a doença e que, logo, o humano provavelmente transmitiu o coronavírus ao animal, e não o contrário. Além disso, Bruno ressalta que quase nenhum pet apresentou doença clínica, “eles só foram infectados e tiveram o diagnóstico da presença do vírus”.</p>
                                <p>Portanto, na maioria dos casos, os animais não apresentaram sintomas como os humanos, apenas diagnóstico positivo pela detecção do agente infeccioso causador do coronavírus. De acordo com o veterinário, gatos que testaram positivo apresentaram apenas alterações respiratórias e, depois, voltaram a normalidade.</p>
                                <p>Caso um animal doméstico seja diagnosticado com coronavírus, Bruno recomenda que os donos sigam as mesmas recomendações já conhecidas, como restringir o contato entre pacientes positivos de COVID-19 com humanos e também com seus pets, por precaução. “Sabemos que existe uma possibilidade do humano disseminar o vírus para os pets,  mas não sabemos claramente se o COVID-19 pode causar alterações na saúde desses animais, então não vamos arriscar”, explica.</p>
                                <p>Sobre o cão sacrificado na Carolina do Sul, Bruno explica que o dono do animal foi diagnosticado com COVID-19. De acordo com o veterinário, foi identificada a presença do vírus no cachorro, mas não foi confirmado que o cão desenvolveu um quadro clínico causado pelo coronavírus. A eutanásia foi realizada pela idade avançada do animal e pela presença de doenças crônicas.</p>
                                <p>O médico veterinário informou que diversos centros de pesquisas nacionais e internacionais estão focados em compreender melhor a atividade desse vírus e sua interação tanto no homem como em animais. “Os resultados até então presentes revelam a baixa chance de transmissão desse vírus do animal para o humano”, concluiu.</p>
