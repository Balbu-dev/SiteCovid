                                <p>Meio sem acreditar a princípio, vimos a epidemia da Covid-19 chegar, se espalhar e tomar a proporção que tem hoje. Esta situação inusitada nos trouxe uma série de incertezas: Estamos achatando a curva? Quando teremos uma vacina disponível? De repente, a saúde assume um papel prioritário nas discussões e voltamos a lembrar de sua importância não apenas como indivíduos, mas como sociedade.</p>
                                <p>Além de médica, atuo com projetos de impacto positivo para grandes empresas, governos e organizações. Já há algum tempo me dedico a buscar soluções para os desafios de saúde do país – de preferência soluções exponenciais, que possam ser facilmente escaladas. Desde o início da epidemia, até por estar distante da linha de frente, busquei me envolver em iniciativas que pudessem contribuir para o enfrentamento da doença. Foram dias incansáveis, analisando a literatura disponível em busca das melhores evidências científicas, me conectando com grupos nacionais e internacionais (de médicos, de inovação, do movimento maker), acompanhando as coletivas de imprensa, a mídia local e a internacional. Foram dias intensos e tive muito prazer em participar de algumas iniciativas nas quais, junto com clientes queridos como a Ambev, descobrimos soluções com resultados fantásticos: a fabricação de videolaringoscópios em impressoras 3D a baixíssimo custo, a doação de 3 milhões de face shields feitos com PET de garrafas e a distribuição de mais de 50.000 kits de EPIs para médicos.</p>
                                <p>Em meio a uma falta de direcionamento nacional, acompanhei com orgulho a mobilização da sociedade e tantos exemplos maravilhosos de solidariedade. Mas, apesar de todos os esforços, ainda estamos no meio desta crise e com um futuro cheio de incertezas. O coronavírus continua aqui e não acho que conseguiremos eliminá-lo no curto prazo. Teremos que aprender a conviver com ele no futuro. Para mim, uma coisa é certa, só sairemos desta crise juntos.</p>
                                <p>Governos, empresas e organizações sociais partem em busca de soluções. Para as empresas, o desafio é manter a continuidade dos negócios, ao mesmo tempo que garantem a segurança de suas equipes e clientes. Além das medidas impostas pelas normas regulamentadoras, que tornaram obrigatórias uma série de ações de prevenção, mitigação e controle da Covid-19, as empresas deverão ter seus processos minuciosamente pensados para que a prevenção seja efetiva na sua realidade.</p>
                                <p>Pensando nisto – e buscando somar meu conhecimento a alguma iniciativa que pudesse contribuir efetivamente para reduzirmos o impacto da epidemia, criamos o SaúdeX, uma plataforma de gestão Covid-19 com base médica científica que oferece uma solução simples para facilitar a prevenção e a identificação precoce de casos da Covid-19, com um bot para monitoramento de sintomas e testagem em escala.</p>
                                <p>Quando e como voltar ao trabalho depende do perfil de cada empresa e da situação epidemiológica local e, por isso, não existe uma solução única para todas as empresas, mas, algumas práticas em comum podem ser úteis:</p>
                                <ul>
                                    <li>Criar um plano de resposta nem sempre é fácil, mas não dá para esperar por uma solução mágica. Tenha pronto um plano e esteja atento para adaptá-lo continuamente de acordo com a evolução da realidade. Assegure-se de estar de acordo com as normas e legislações e atue de forma coordenada com a gestão do governo local.</li>
                                    <li>Repense suas atividades com a mente aberta. Muitas funções podem (e devem) ser realizadas de forma remota sem nenhum prejuízo aos resultados. Reduzir o número de funcionários presenciais ao mínimo é a opção mais segura, além de ser uma oportunidade para reduzir custos.</li>
                                    <li>Conheça o perfil de risco dos seus funcionários. Privilegie o retorno precoce dos que têm menor risco, já que o impacto da doença em certas populações é muito mais grave. Questões pessoais, como a existência de filhos em idade escolar ou a convivência com idosos também devem ser levadas em consideração.</li>
                                    <li>Na volta ao trabalho, assegure-se de implantar as medidas padrão de prevenção como checagem de temperatura, distanciamento, uso de máscaras e higienização. Elas podem ajudar a diminuir o risco. Lembre-se de indicar responsáveis para coordenar as ações.</li>
                                    <li>Reduza a transmissão entre os trabalhadores. Dividir os funcionários em grupos de contato pode ser útil para facilitar o isolamento de suspeitos e evitar o afastamento de muitas pessoas em caso de aparecimento de alguma suspeita.</li>
                                    <li>Monitore a saúde dos colaboradores. Crie um ambiente para facilitar o relato de qualquer sintoma, assegurando que não existirá nenhum tipo de prejuízo ou punição para o funcionário.</li>
                                    <li>Facilite o acesso a testagem. Estabeleça uma estratégia e defina quem será testado, quando e onde ela será realizada. Certifique-se de escolher testes com comprovação científica da eficácia e que sejam adequados para cada situação.</li>
                                    <li>Capriche na comunicação. É preciso mostrar assertividade, sabendo transmitir informações, engajando os trabalhadores nas medidas de prevenção para que se sintam seguros e confiantes no seu dia a dia.</li>
                                    <li>Incentive seus funcionários a manterem as medidas de prevenção nos locais de convívio na empresa, assim como fora do trabalho e em casa. A segurança de cada um depende do cuidado de todos, em todos os momentos.</li>
                                    <li>Traga humanidade para dentro do trabalho. O momento é intenso, muitas pessoas passam por situações pessoais difíceis e as questões de saúde mental estão muito presentes. Mais do que nunca, precisamos estar atentos para acolher e promover um ambiente de trabalho positivo.</li>
                                </ul>
                                <p>Ao mesmo tempo que temos um gigantesco desafio pela frente, esta crise nos traz uma oportunidade imperdível de repensar a saúde, indo além do “normal”. Os líderes empresariais têm um importante papel é necessário sair da zona de conforto. Juntos podemos avançar em novos modelos e encontrar caminhos onde a promoção da saúde possa beneficiar a sociedade, a economia e os negócios.</p>
