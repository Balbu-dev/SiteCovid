                    <p class="font-weight-bold text-center align-middle display-4">Impactos negativos da covid-19 nas empresas diminuíram em agosto<p>
                </div>
                <div class="container">
                    <div class="container pt-3 pb-3">
                        <div class="container mb-3">
                            <img src="../../../images/news/temas-de-interesse-da-cni_44308476691_o.jpg" class="rounded mx-auto d-block img-fluid w-50" alt="Saúde Logo" id="logo">
                        </div>
                        <div class="container">
                            <div class="row h-75 text-justify">
                                <p>Os impactos negativos da pandemia da covid-19 foram sentidos por 33,5% das 3,4 milhões de empresas brasileiras na segunda quinzena de agosto, contra 38,6% medidos no período anterior. Na primeira quinzena de junho, o efeito negativo foi sentido por 70% do total. Outras 37,9% tiveram impacto pequeno ou inexistente e 28,6% sentiram efeitos positivos com a crise sanitária na segunda quinzena de agosto. Os dados foram divulgados hoje pelo Instituto Brasileiro de Geografia e Estatística (IBGE), na Pesquisa Pulso Empresa: Impacto da Covid19 nas Empresas.</p>
                                <p>Empresas de todos os portes relataram a melhora na percepção. Segundo o IBGE, os efeitos pequenos ou inexistentes foram sentidos por 52,6% das empresas de grande porte, 43,3% das médias e por 37,8% das menores. As de porte intermediário tiveram a maior percepção dos efeitos positivos, com 33,8%.</p>
                                <p>De acordo com o coordenador de Pesquisas Conjunturais em Empresas do IBGE, Flávio Magheli, nos primeiros meses da crise sanitária os impactos estavam relacionados à demanda por vendas, produção e atendimento, com o fechamento das lojas e o isolamento social. Depois, os problemas foram de oferta e acesso à cadeia de suprimentos. Com a flexibilização das medidas restritivas, está ocorrendo um processo de retomada gradual.</p>
                                <h2>Atividades</h2>
                                <p>As empresas de construção (40,0%) e do comércio (36,0%) foram as que relataram mais efeitos negativos na quinzena, as mesmas atividades da quinzena anterior. A indústria (40,3%) e o setor de serviços (43,2%) relataram mais impactos pequenos ou inexistentes, com destaque para os segmentos de serviços de informação e comunicação (68,7%) e serviços de transporte (48,8%). Segundo Magheli, o perfil do impacto mudou no decorrer da pandemia.</p>
                                <p>“Do ponto de vista setorial, no início da pesquisa, há uma incidência forte de dificuldades na indústria, na construção, nos serviços e principalmente no comércio, devido à grande dependência dos pequenos comércios em relação às lojas físicas. Ao longo desses três meses, ocorreu uma retomada gradual, mas no final de agosto 33,5% das empresas ainda sinalizam algum grau de dificuldade”.</p>
                                <p>Na análise regional, no período analisado os efeitos foram pequenos ou inexistentes para 37,4% das empresas na região Norte; 37,3% no Sudeste; 42,9% no Sul e 40,7% no Centro-Oeste. Com a maior abertura econômica nos estados do nordeste, a região se destaca com 45% das empresas relatando efeitos positivos na segunda quinzena de agosto.</p>
                                <h2>Vendas</h2>
                                <p>A redução nas vendas foi percebido por 32,9% das empresas, 34,7% tiveram impactos pequenos ou inexistentes e 32,2% relataram aumento nas vendas. O setor mais afetado pela redução nas vendas foi a construção, com 42,7%. O aumento foi sentido por 40,7% das empresas do comércio, sendo 43% no comércio varejista e 46,6% comércio de veículos, peças e motocicletas.</p>
                                <p>Apenas 13,9% das empresas relataram facilidade na capacidade de fabricar produtos ou atender clientes, enquanto 31,4% tiveram dificuldades e 54,4% não tiveram alteração significativa. A dificuldade no acesso ao fornecedor foi relatada por 46,8% e 44,1% não perceberam alteração. Obstáculos para realizar pagamentos de rotina foi sentida por 40,3% das empresas e 53% não tiveram alteração significativa.</p>
                                <p>Segundo os dados do IBGE, na segunda quinzena de agosto 85% das empresas em funcionamento mantiveram o número de colaboradores em relação ao período anterior, 8,1% diminuíram e 6,3% ampliaram o quadro de pessoal. Das 280 mil empresas que demitiram, 56,8% diminuíram o quadro em até 25%, sendo 55,8% delas empresas de menor porte, com até 49 trabalhadores.</p>
                                <p>Entre as medidas de prevenção da pandemia foram relatadas por 93,1% das empresas a realização de campanhas de informação e medidas extras de higiene, 28,6% mudaram o método de entrega, 25,7% adotaram o trabalho remoto, 20,1% anteciparam férias dos funcionários e 23,8% adiaram o pagamento de impostos.</p>
                                <p>O apoio governamental para adotar medidas para reduzir os impactos da pandemia foi relatado por 21,4% das empresas, sendo 47,9% das empresas que adiaram o pagamento de impostos e 61,6% das que acessaram linhas de crédito para o pagamento da folha salarial.</p>
                                <p>De acordo com Magheli, a situação geral das empresas melhorou, mas ainda não foram recuperadas as perdas provocadas pela pandemia da covid-19.</p>
                                <p>“Ao longo desses três meses, a percepção das empresas melhorou, mas o efeito de diminuição sobre as vendas, redução na capacidade de fabricar produtos ou atender clientes, dificuldades em acessar fornecedores e insumos e realizar pagamentos ainda faz parte da rotina das empresas”.</p>
