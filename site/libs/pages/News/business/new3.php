                                <h2>Importância</h2>
                                <p>A comissão mista do Congresso Nacional que fiscaliza as ações do governo no combate à pandemia de coronavírus realiza audiência pública remota, amanhã, para debater as dificuldades de acesso ao crédito por parte das micros e pequenas empresas e dos microempreendedores individuais, o que compromete a retomada do crescimento econômico. Entre os convidados estão Rosi Dedekind, presidente da Federação das Associações de Micro e Pequenas Empresas e dos Empreendedores Individuais de SC (Fampesc), e Adael dos Santos, presidente da Associação de Joinville e Região de Pequenas, Micro e Médias Empresas (Ajorpeme).</p>
                                <h2>Segredo bilionário</h2>
                                <p>“Qual é o segredo da Weg, a fábrica de bilionários? A indagação foi feita pelo “Estadão”, ontem, sobre a multinacional catarinense, a propósito dela ter, entre seus fundadores e herdeiros, 10 dos 33 novos bilionários brasileiros, segundo a mais recente lista divulgada pela revista Forbes. O primeiro: a impressionante valorização das ações da empresa nos últimos 12 meses, de mais de 240%, o que elevou seu valor de mercado para R$ 150 bilhões. Outros segredos: a resiliência demonstrada na atual pandemia, a consistência das suas operações, creditadas à sua longeva equipe de executivos (em 59 anos de existência, a gigante de Jaraguá do Sul teve só três presidentes), sempre crescer e abrir fronteiras. Talvez sem querer, estejam dando muitos e ótimos exemplos a uma boa parte da classe empresarial catarinense. Quais, não precisa dizer.</p>
                                <h2>Impeachment</h2>
                                <p>Ouvido pelo jornal “O Globo”, o respeitado cientista político Cristiano Noronha admite que “não parece haver ilegalidade nos processos de impeachment em curso” do governador Carlos Moisés, mas a existência dos processos contra ele “mostram a necessidade de avaliação das relações políticas entre os poderes, a importância de ter uma relação harmoniosa entre Executivo e Legislativo; não submissa, mas de respeito mútuo”. Confere.</p>
                                <h2>Desinchaço</h2>
                                <p>Alguns candidatos a prefeito e vereador em SC estão conseguindo apoios inesperados ao destacar como bandeira de suas campanhas a redução dos cargos comissionados nas prefeituras. Esse é o corajoso propósito, por exemplo, do empresário Claudir Bittencourt (PSDB), que quer ser prefeito de Capivari de Baixo. Quem segui-lo certamente ganhará apoio (não necessariamente votos) da população.</p>
                                <h2>Udesc ampliada</h2>
                                <p>A Universidade do Estado (Udesc) é normalmente esquecida nas emendas parlamentares de deputados estaduais. Pois ela resolveu agir de forma diferente: agora vai levar propostas a eles. É o que está articulando no momento em dois projetos: um de extensão sobre cicloturismo envolvendo 13 municípios do Oeste e outro de expansão de programa de compostagem de lixo orgânico a partir do que é feito com muito sucesso e adesão em Lages.</p>
                                <h2>Mofo</h2>
                                <p>Dezenove anos depois, por maioria, o Supremo Tribunal Federal finalmente julgou, considerando-a inconstitucional, a lei estadual de SC 11.707/2001, que obrigam lojas e operadoras de telefonia móvel a fazerem o cadastro com dados pessoais do consumidor para a venda de aparelhos e chip de celular na modalidade pré-paga. E na última sexta-feira, numa decisão extraordinariamente rápida, o mesmo STF deu a liberdade a um dos maiores líderes do bando criminoso PCC. Essa é a nossa chamada “Corte Suprema”! (entre aspas, porque se questiona cada vez mais seu papel e importância) Socorro!</p>
                                <h2>Insegurança</h2>
                                <p>O vai-e-vem das restrições impostas ao setor hoteleiro de SC diante das classificações de risco para a covid-19, cujas cores do mapa determinam o número de quartos ocupados, é mais um desserviço ao setor – um dos mais prejudicados na retomada. Na opinião de Estanislau Bresolin, presidente da Federação de Hotéis, Restaurantes, Bares e Similares de SC (Fhoresc), o segmento não pode ficar à mercê desta oscilação quase diária, já que trabalha com reservas de semanas ou meses. E arremata: “Se é impensável restringir o número de apartamentos ocupados num condomínio residencial, por que limitar quartos de um hotel? Ora, que as normas sirvam somente às áreas comuns”.</p>
                                <h2>Espelho retrovisor</h2>
                                <p>Em rede social, e com direito a aplausos, o veterano cronista esportivo Paulo Brito bate no saudosismo exacerbado que há na Ilha de SC, daqueles que não querem asfalto sobre os paralelepípedos desconjuntados na Praça XV, por exemplo. Pergunta porque não pedem para colocar piso de madeira na Ponte Hercílio Luz, porque é histórico? Se querem a volta dos lampiões na Praça XV, aqueles que usavam óleo de baleia? Conclui que se é para conservar a história, que tal voltar a matar baleias para tirar o óleo e o espermacete para fazer velas? Não lhes dar razão?</p>
