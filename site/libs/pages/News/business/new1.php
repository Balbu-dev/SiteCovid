                                <div id="textoMateria">
                                    <p>Aprovadas na quinta-feira (1°), as Medidas Provisórias (MP) 976/2020 e MP 977/2020 viraram leis. A primeira (Lei 14.067) abriu crédito extraordinário de R$ 4,489 bilhões ao Ministério da Saúde para ações de combate à pandemia de coronavírus e a segunda (Lei 14.068) abriu crédito extraordinário de R$ 20 bilhões para a União conceder garantia a empréstimos feitos pelos bancos a empresas, iniciativa tomada também em razão da crise sanitária. Promulgadas pelo presidente do Congresso, Davi Alcolumbre, as duas estão publicadas na edição desta sexta-feira (2) do Diário Oficial da União.</p>
                                    <p>A maior parte dos recursos (R$ 4,469 bilhões) previstos na Lei 14.067 foi destinada ao Fundo Nacional de Saúde (FNS), que os está transferindo a estados e municípios para custeio de serviços de saúde, para pagamento de bolsas ou bonificação aos estudantes universitários da área da saúde e médicos residentes e para ampliação da conectividade à internet de unidades de atenção primária.</p>
                                    <p>Segundo o governo federal, essa ampliação é conduzida pela Rede Nacional de Ensino e Pesquisa, organização social vinculada ao Ministério da Ciência, Tecnologia e Inovações.</p>
                                    <p>Ainda desse montante, R$ 23 milhões estão direcionados especificamente ao Hospital Nossa Senhora da Conceição, em Porto Alegre, para compra de equipamentos de proteção individual, compra e aluguel de respiradores e monitores e compra de testes e exames da covid-19.</p>
                                    <p>Os outros R$ 20 milhões ficam com o Sistema Único de Saúde (SUS) para contratos de gestão com organizações sociais.</p>
                                    <h2>Empresas</h2>
                                    <p>A intenção do governo com a Lei 14.068 é resguardar pequenas e médias empresas afetadas pela crise econômica causada pela pandemia da covid-19. Além dessas empresas, também poderão ter acesso ao financiamento com garantia as associações, as fundações de direito privado e as sociedades cooperativas, exceto as de crédito.</p>
                                    <p>O crédito será utilizado para a integralização de cotas junto ao Fundo Garantidor para Investimentos (FGI), que visa a garantir operações de crédito a pequenas e médias empresas, atendendo a demanda do Programa Emergencial de Acesso a Crédito (Peac-FGI).</p>
                                    <p>Esse programa é voltado às empresas com faturamento bruto anual entre R$ 360 mil e R$ 300 milhões, no ano calendário de 2019. O objetivo é garantir via FGI os empréstimos a essas empresas. Esse programa será somado ao Programa Nacional de Apoio às Microempresas e Empresas de Pequeno Porte (Pronampe), instituído pela Lei 13.999, de 2020.</p>
                                    <p>O aporte inicial será de R$ 5 bilhões, e as parcelas seguintes serão liberadas conforme a demanda do mercado de crédito por garantias. O limite é de R$ 20 bilhões. O dinheiro virá da emissão de títulos do Tesouro Nacional.</p>
                                    <h2>Orçamento</h2>
                                    <p>O crédito extraordinário é destinado a despesas urgentes e imprevistas, em caso de guerra ou calamidade pública, e é encaminhado ao Congresso Nacional pelo presidente da República por meio de medida provisória.</p>
